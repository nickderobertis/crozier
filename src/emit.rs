/// The header comment crozier writes atop every generated file. It differs from
/// Fern's by design; the comment-stripping normalizer removes it before any
/// comparison, so the wording is free.
const HEADER: &str = "# This file was auto-generated by crozier from an OpenAPI document.";

/// Escape a spec-controlled value for embedding inside a double-quoted Python
/// string literal, so a `"`/`\`/newline in the input cannot break out of (or
/// inject into) the generated source. Ordinary values (URLs, header names) pass
/// through unchanged, so byte-exact output is unaffected.
fn escape_py_str(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
}

/// A generated file: a repo-relative path and its contents.
#[derive(Debug, Clone)]
pub struct GeneratedFile {
    /// Path relative to the output root.
    pub path: PathBuf,
    /// File contents.
    pub contents: String,
}

/// Python standard-library top-level modules crozier may import. Used to split
/// imports into Fern's two ordered groups (stdlib first, everything else second).
const STDLIB_MODULES: &[&str] = &[
    "typing",
    "datetime",
    "uuid",
    "importlib",
    "enum",
    "os",
    "sys",
    "json",
    "base64",
    "collections",
    "decimal",
    "abc",
    "contextlib",
    "logging",
];

/// The location of the file whose imports are being collected, relative to the
/// package root — it decides the relative path to a referenced generated type
/// (how many leading dots, and whether via a tag's `types/` subpackage).
#[derive(Clone, Default)]
enum RefLoc {
    /// A generated file at the package root (`src/{pkg}/client.py` or
    /// `src/{pkg}/raw_client.py`).
    PackageRoot,
    /// A module in the package-root `types/` package (`src/{pkg}/types/`).
    #[default]
    RootTypes,
    /// A module in a tag's `types/` package (`src/{pkg}/{tag}/types/`).
    TagTypes(String),
    /// A tag client file (`src/{pkg}/{tag}/raw_client.py` or `client.py`).
    Client(String),
    /// The `errors/` package (`src/{pkg}/errors/`).
    Errors,
}

/// Collects imports and renders them in Fern's order: group 1 is stdlib
/// (`import`s then `from`s), group 2 is everything else (`import`s then `from`s),
/// separated by a blank line. Names within a `from` and the statements within a
/// group are sorted.
#[derive(Default)]
struct Imports {
    /// Plain `import module [as alias]`, keyed by module for sorting.
    plain: BTreeMap<String, Option<String>>,
    /// `from module import a, b`, module -> sorted names.
    from: BTreeMap<String, BTreeSet<String>>,
    /// This file's location, for resolving relative imports of generated types.
    loc: RefLoc,
    /// Names of tag-scoped (hoisted) types → their owning tag directory. A name
    /// absent from this map is a package-root type.
    tag_types: BTreeMap<String, String>,
    /// Referenced type names that must render as string forward references
    /// (`"TreeNode"`) because importing them eagerly would close an import cycle
    /// (issue #84). A same-file self-reference needs no import at all; a
    /// cross-module cycle import is deferred to [`Imports::deferred`].
    forward: std::collections::HashSet<String>,
    /// The module (file stem) of the type being emitted, so a forward reference to
    /// a type in *this* file is recognised and left un-imported.
    cur_module: String,
    /// `from … import …` lines for forward references in *other* modules, emitted
    /// after the class body (a deferred import) so the module loads without the
    /// cycle firing at import time.
    deferred: BTreeSet<String>,
}

impl Imports {
    /// A fresh collector for a file at `loc`, aware of the tag-scoped type map so
    /// references resolve to the right relative path.
    fn at(loc: RefLoc, tag_types: &BTreeMap<String, String>) -> Self {
        Imports {
            loc,
            tag_types: tag_types.clone(),
            ..Default::default()
        }
    }

    /// The relative module path a referenced generated type is imported from,
    /// chosen from this file's [`RefLoc`] and whether the type is tag-scoped.
    fn type_import_path(&self, class: &str) -> String {
        let m = naming::module_name(class);
        match self.tag_types.get(class) {
            Some(tag) if tag.is_empty() => match &self.loc {
                RefLoc::RootTypes => format!(".{m}"),
                RefLoc::TagTypes(_) => format!("...types.{m}"),
                RefLoc::Client(_) | RefLoc::Errors => format!("..types.{m}"),
                RefLoc::PackageRoot => format!(".types.{m}"),
            },
            // A tag-scoped type lives at `{pkg}.{tag}.types.{m}`.
            Some(tag) => match &self.loc {
                RefLoc::TagTypes(cur) if cur == tag => format!(".{m}"),
                RefLoc::Client(cur) if cur == tag => format!(".types.{m}"),
                RefLoc::TagTypes(_) | RefLoc::Client(_) | RefLoc::Errors => {
                    format!("..{tag}.types.{m}")
                }
                RefLoc::PackageRoot | RefLoc::RootTypes => format!(".{tag}.types.{m}"),
            },
            // A package-root type lives at `{pkg}.types.{m}`.
            None => match &self.loc {
                RefLoc::RootTypes => format!(".{m}"),
                RefLoc::TagTypes(_) => format!("...types.{m}"),
                RefLoc::Client(_) | RefLoc::Errors => format!("..types.{m}"),
                RefLoc::PackageRoot => format!(".types.{m}"),
            },
        }
    }

    /// Register the import for a referenced generated type. A type flagged as a
    /// forward reference (issue #84) is *not* imported eagerly: a same-file
    /// reference needs no import, and a cross-module one is deferred to after the
    /// class body so its cycle does not fire at import time.
    fn add_type(&mut self, class: &str) {
        if self.forward.contains(class) {
            if naming::module_name(class) != self.cur_module {
                let path = self.type_import_path(class);
                self.deferred.insert(format!("from {path} import {class}"));
            }
            return;
        }
        let path = self.type_import_path(class);
        self.add_from(&path, class);
    }

    /// The dotted prefix that reaches the package-root `core` package from this
    /// file's location. Every generated file sits one package below the root
    /// (`{pkg}/types/`, `{pkg}/{tag}/`, `{pkg}/errors/`) except a tag's `types/`
    /// (`{pkg}/{tag}/types/`), which is two deep and needs the extra dot.
    fn core_prefix(&self) -> &'static str {
        match self.loc {
            RefLoc::TagTypes(_) => "...core",
            RefLoc::RootTypes | RefLoc::Client(_) | RefLoc::Errors => "..core",
            RefLoc::PackageRoot => ".core",
        }
    }

    fn error_import_path(&self, module: &str) -> String {
        match self.loc {
            RefLoc::PackageRoot => format!(".errors.{module}"),
            _ => format!("..errors.{module}"),
        }
    }

    /// Register a `from {..}core.{submodule} import {name}` at the depth this
    /// file's location requires — so `core.*` helpers resolve uniformly rather
    /// than with a hardcoded dot count that only holds at the root nesting level
    /// (issue #85).
    fn add_core(&mut self, submodule: &str, name: &str) {
        let module = format!("{}.{submodule}", self.core_prefix());
        self.add_from(&module, name);
    }

    fn add_core_package(&mut self) {
        let parent = self.core_prefix().trim_end_matches("core");
        self.add_from(parent, "core");
    }

    fn add_plain(&mut self, module: &str) {
        self.plain.entry(module.to_string()).or_insert(None);
    }

    fn add_plain_as(&mut self, module: &str, alias: &str) {
        self.plain
            .insert(module.to_string(), Some(alias.to_string()));
    }

    fn add_from(&mut self, module: &str, name: &str) {
        self.from
            .entry(module.to_string())
            .or_default()
            .insert(name.to_string());
    }

    /// Is a module (or a `from` path's leading segment) part of the stdlib group?
    /// Relative imports (leading `.`) are never stdlib.
    fn is_stdlib(module: &str) -> bool {
        if module.starts_with('.') {
            return false;
        }
        let top = module.split('.').next().unwrap_or(module);
        STDLIB_MODULES.contains(&top)
    }

    fn render(&self) -> String {
        let (mut g1, mut g2): (Vec<String>, Vec<String>) = (Vec::new(), Vec::new());

        // Plain imports first within each group (BTreeMap iterates sorted).
        for (module, alias) in &self.plain {
            let line = match alias {
                Some(a) => format!("import {module} as {a}"),
                None => format!("import {module}"),
            };
            if Self::is_stdlib(module) {
                g1.push(line);
            } else {
                g2.push(line);
            }
        }
        // Then `from` imports within each group.
        for (module, names) in &self.from {
            let joined = names.iter().cloned().collect::<Vec<_>>().join(", ");
            let line = format!("from {module} import {joined}");
            if Self::is_stdlib(module) {
                g1.push(line);
            } else {
                g2.push(line);
            }
        }

        let groups: Vec<String> = [g1, g2]
            .into_iter()
            .filter(|g| !g.is_empty())
            .map(|g| g.join("\n"))
            .collect();
        groups.join("\n\n")
    }
}

/// Render a resolved type to a [`Doc`] expression, registering needed imports.
/// The `Doc` renders flat; `ruff format` handles any line wrapping downstream.
fn render_type(t: &TypeRef, imports: &mut Imports) -> Doc {
    match t {
        TypeRef::Primitive(p) => match p {
            Prim::Str => Doc::atom("str"),
            Prim::Int | Prim::Long => Doc::atom("int"),
            Prim::Float => Doc::atom("float"),
            Prim::Bool => Doc::atom("bool"),
            Prim::Any => {
                imports.add_plain("typing");
                Doc::atom("typing.Any")
            }
            Prim::Datetime => {
                imports.add_plain_as("datetime", "dt");
                Doc::atom("dt.datetime")
            }
            Prim::Date => {
                imports.add_plain_as("datetime", "dt");
                Doc::atom("dt.date")
            }
        },
        TypeRef::Named(class) => {
            imports.add_type(class);
            // A forward reference (issue #84) renders as a quoted string so the
            // annotation needs no eager import of a not-yet-defined name.
            if imports.forward.contains(class) {
                Doc::atom(format!("\"{class}\""))
            } else {
                Doc::atom(class.clone())
            }
        }
        TypeRef::Optional(inner) => {
            imports.add_plain("typing");
            Doc::group("typing.Optional[", vec![render_type(inner, imports)], "]")
        }
        TypeRef::Literal(values) => {
            imports.add_plain("typing");
            let items = values
                .iter()
                .map(|v| Doc::atom(format!("\"{v}\"")))
                .collect();
            Doc::group("typing.Literal[", items, "]")
        }
        TypeRef::List(inner) => {
            imports.add_plain("typing");
            Doc::group("typing.List[", vec![render_type(inner, imports)], "]")
        }
        TypeRef::Set(inner) => {
            imports.add_plain("typing");
            Doc::group("typing.Set[", vec![render_type(inner, imports)], "]")
        }
        TypeRef::Dict(k, v) => {
            imports.add_plain("typing");
            Doc::group(
                "typing.Dict[",
                vec![render_type(k, imports), render_type(v, imports)],
                "]",
            )
        }
        TypeRef::Union(variants) => {
            imports.add_plain("typing");
            let items = variants.iter().map(|v| render_type(v, imports)).collect();
            Doc::group("typing.Union[", items, "]")
        }
    }
}

/// A field prepared for emission: its Python name, resolved annotation, the
/// `= <default>` suffix, and an optional docstring.
struct RenderedField {
    py_name: String,
    annotation: Doc,
    default: String,
    docstring: Option<String>,
}

/// The view model handed to `object.py.j2`. Rust computes the semantics (imports,
/// per-field annotation strings, defaults); the template lays out the class body
/// (field order, docstrings, the blank-line rules `ruff` does not impose) and
/// `ruff format` handles the line wrapping. This split keeps the byte-sensitive
/// logic in testable Rust while the file's *shape* lives in the template.
#[derive(Serialize)]
struct ObjectView {
    header: &'static str,
    imports: String,
    class_name: String,
    bases: String,
    /// The class docstring's raw text (the template applies the `docstring` filter).
    docstring: Option<String>,
    fields: Vec<FieldView>,
    /// The `pydantic.ConfigDict(...)` argument list for the v2 `model_config`
    /// (`extra="allow", frozen=True`, or just `frozen=True` for `ignore`).
    config_dict_args: String,
    /// The v1 `Config`'s `pydantic.Extra.<extra_kind>` member (`allow`/`ignore`/
    /// `forbid`).
    extra_kind: &'static str,
}

/// One field as the object template consumes it: the flat declaration line
/// (`name: annotation = default`, wrapped later by `ruff`) and the raw docstring.
#[derive(Serialize)]
struct FieldView {
    decl: String,
    docstring: Option<String>,
}

/// Compute a field's annotation + default, registering imports.
fn render_field(field: &Field, imports: &mut Imports) -> RenderedField {
    let inner = render_type(&field.type_ref, imports);
    let typ = if field.optional {
        imports.add_plain("typing");
        Doc::group("typing.Optional[", vec![inner], "]")
    } else {
        inner
    };

    // Fern carries the wire alias in `FieldMetadata` inside the `Annotated`, not
    // in `pydantic.Field`.
    let annotation = if field.needs_alias() {
        imports.add_plain("typing_extensions");
        imports.add_core("serialization", "FieldMetadata");
        Doc::group(
            "typing_extensions.Annotated[",
            vec![
                typ,
                Doc::atom(format!("FieldMetadata(alias=\"{}\")", field.wire_name)),
            ],
            "]",
        )
    } else {
        typ
    };

    RenderedField {
        py_name: field.py_name.clone(),
        annotation,
        default: field_default(field, imports),
        docstring: field.docstring.clone(),
    }
}

/// The `= <default>` suffix for a field. A documented field carries a
/// `pydantic.Field` (with `default=None` when optional); an undocumented
/// optional field defaults to `None`; a required undocumented field has none.
fn field_default(field: &Field, imports: &mut Imports) -> String {
    if field.docstring.is_some() {
        imports.add_plain("pydantic");
        if field.optional {
            " = pydantic.Field(default=None)".to_string()
        } else {
            " = pydantic.Field()".to_string()
        }
    } else if field.optional {
        " = None".to_string()
    } else {
        String::new()
    }
}

/// Build the minijinja environment with the embedded templates.
fn environment() -> Environment<'static> {
    let mut env = Environment::new();
    env.set_keep_trailing_newline(true);
    env.set_trim_blocks(true);
    env.set_lstrip_blocks(true);
    // Render a raw docstring into an indented, backslash-escaped triple-quoted
    // block; templates apply it with `{{ text | docstring }}`.
    env.add_filter("docstring", |text: String| render_docstring(&text, 4));
    env.add_template("version.py", include_str!("../templates/version.py.j2"))
        .expect("version template compiles");
    env.add_template("object.py", include_str!("../templates/object.py.j2"))
        .expect("object template compiles");
    env.add_template(
        "class_body.py",
        include_str!("../templates/class_body.py.j2"),
    )
    .expect("class_body template compiles");
    env.add_template("alias.py", include_str!("../templates/alias.py.j2"))
        .expect("alias template compiles");
    env.add_template(
        "raw_client.py",
        include_str!("../templates/raw_client.py.j2"),
    )
    .expect("raw_client template compiles");
    env.add_template("error.py", include_str!("../templates/error.py.j2"))
        .expect("error template compiles");
    env.add_template("lazy_init.py", include_str!("../templates/lazy_init.py.j2"))
        .expect("lazy_init template compiles");
    env.add_template(
        "reference_entry.md",
        include_str!("../templates/reference_entry.md.j2"),
    )
    .expect("reference_entry template compiles");
    env.add_template(
        "root_client.py",
        include_str!("../templates/root_client.py.j2"),
    )
    .expect("root_client template compiles");
    env.add_template(
        "client_class.py",
        include_str!("../templates/client_class.py.j2"),
    )
    .expect("client_class template compiles");
    env.add_template("file.py", include_str!("../templates/file.py.j2"))
        .expect("file template compiles");
    env
}

/// Generate every file crozier currently emits for an IR.
/// Collect every named-type reference reachable within a resolved type.
fn collect_named_refs(t: &TypeRef, out: &mut Vec<String>) {
    match t {
        TypeRef::Named(n) => out.push(n.clone()),
        TypeRef::Optional(i) | TypeRef::List(i) | TypeRef::Set(i) => collect_named_refs(i, out),
        TypeRef::Dict(k, v) => {
            collect_named_refs(k, out);
            collect_named_refs(v, out);
        }
        TypeRef::Union(vs) => vs.iter().for_each(|v| collect_named_refs(v, out)),
        TypeRef::Primitive(_) | TypeRef::Literal(_) => {}
    }
}

/// The named types a declaration references (base classes plus every field's
/// type), for building the type-reference graph that finds recursive cycles.
fn decl_refs(decl: &TypeDecl) -> Vec<String> {
    let mut out = Vec::new();
    match decl {
        TypeDecl::Object(o) => {
            out.extend(o.bases.iter().cloned());
            for f in &o.fields {
                collect_named_refs(&f.type_ref, &mut out);
            }
        }
        TypeDecl::Alias(a) => collect_named_refs(&a.target, &mut out),
        TypeDecl::DiscriminatedUnion(u) => {
            // A union can be any of its mapped variant schemas — that edge is what
            // exposes a variant that recurses back through the union (issue #84).
            out.extend(u.variant_targets.iter().cloned());
            for m in &u.members {
                for f in &m.fields {
                    collect_named_refs(&f.type_ref, &mut out);
                }
            }
        }
        TypeDecl::Enum(_) => {}
    }
    out
}

/// For every generated type, the subset of its own references that close a cycle
/// back to it — the references crozier must emit as string forward references
/// with a deferred/omitted import so the module graph loads (issue #84).
///
/// A reference `A → B` is a back-edge when `B` can reach `A` again through the
/// type graph (a self-reference is the degenerate `A → A`). Fern renders exactly
/// these as `"B"` and repairs them at class-definition time with
/// `update_forward_refs`.
fn forward_ref_map(
    types: &[TypeDecl],
    tag_types: &[crate::ir::TagTypeDecl],
) -> std::collections::HashMap<String, std::collections::HashSet<String>> {
    use std::collections::{HashMap, HashSet};

    // Adjacency: type name → the names it references.
    let mut edges: HashMap<String, Vec<String>> = HashMap::new();
    for decl in types.iter().chain(tag_types.iter().map(|t| &t.decl)) {
        edges
            .entry(decl.name().to_string())
            .or_default()
            .extend(decl_refs(decl));
    }

    // Whether `target` is reachable from `start` following ≥1 edge.
    let reaches = |start: &str, target: &str| -> bool {
        let mut stack: Vec<&str> = edges
            .get(start)
            .into_iter()
            .flatten()
            .map(String::as_str)
            .collect();
        let mut seen: HashSet<&str> = HashSet::new();
        while let Some(node) = stack.pop() {
            if node == target {
                return true;
            }
            if seen.insert(node) {
                stack.extend(edges.get(node).into_iter().flatten().map(String::as_str));
            }
        }
        false
    };

    let mut map: HashMap<String, HashSet<String>> = HashMap::new();
    for decl in types.iter().chain(tag_types.iter().map(|t| &t.decl)) {
        let name = decl.name();
        let defer_recursive_target = !matches!(decl, TypeDecl::Alias(_));
        let mut forward = HashSet::new();
        for r in decl_refs(decl) {
            // Render `r` as a string forward reference (deferred import) when it
            // closes a cycle back to this type, OR when `r` is itself recursive (in a
            // cycle of its own). Importing a recursive type eagerly can trip its
            // module's own import cycle even from an acyclic referrer, so Fern defers
            // every reference *into* a cycle, not only the back-edges of one.
            if reaches(&r, name) || (defer_recursive_target && reaches(&r, &r)) {
                forward.insert(r);
            }
        }
        if !forward.is_empty() {
            map.insert(name.to_string(), forward);
        }
    }
    map
}

/// The types that transitively reference a type in a cycle (including the cyclic
/// types themselves). Fern emits `from __future__ import annotations` +
/// `update_forward_refs` for exactly these — even when no individual field of the
/// type is a string forward reference (a `DraftPayment` whose `schedule` field
/// reaches a cycle it is not part of still needs the trailer). Computed by reverse
/// reachability from the cyclic types over the reference graph.
fn types_reaching_cycle(
    types: &[TypeDecl],
    tag_types: &[crate::ir::TagTypeDecl],
) -> std::collections::HashSet<String> {
    use std::collections::{HashMap, HashSet};

    let mut edges: HashMap<String, Vec<String>> = HashMap::new();
    for decl in types.iter().chain(tag_types.iter().map(|t| &t.decl)) {
        edges
            .entry(decl.name().to_string())
            .or_default()
            .extend(decl_refs(decl));
    }
    let reaches = |start: &str, target: &str| -> bool {
        let mut stack: Vec<&str> = edges
            .get(start)
            .into_iter()
            .flatten()
            .map(String::as_str)
            .collect();
        let mut seen: HashSet<&str> = HashSet::new();
        while let Some(node) = stack.pop() {
            if node == target {
                return true;
            }
            if seen.insert(node) {
                stack.extend(edges.get(node).into_iter().flatten().map(String::as_str));
            }
        }
        false
    };

    // A type is cyclic when it can reach itself. Reverse edges, then flood from the
    // cyclic set: every type that can reach a cyclic one is caught.
    let cyclic: Vec<&str> = edges
        .keys()
        .map(String::as_str)
        .filter(|n| reaches(n, n))
        .collect();
    let mut reverse: HashMap<&str, Vec<&str>> = HashMap::new();
    for (from, tos) in &edges {
        for to in tos {
            reverse.entry(to.as_str()).or_default().push(from.as_str());
        }
    }
    let mut out: HashSet<String> = HashSet::new();
    let mut stack: Vec<&str> = cyclic.clone();
    for c in &cyclic {
        out.insert((*c).to_string());
    }
    while let Some(node) = stack.pop() {
        for &pred in reverse.get(node).into_iter().flatten() {
            if out.insert(pred.to_string()) {
                stack.push(pred);
            }
        }
    }
    out
}

pub fn generate(ir: &Ir) -> Result<Vec<GeneratedFile>> {
    let env = environment();
    let mut files = Vec::new();
    let pkg = &ir.package_name;

    // Map each tag-scoped (hoisted) type name to its owning tag directory, so
    // references from anywhere resolve to the right relative import path.
    let tag_map: BTreeMap<String, String> = ir
        .tag_types
        .iter()
        .map(|t| (t.decl.name().to_string(), t.module.clone()))
        .collect();

    // Recursive types: per type, the references that must render as string forward
    // references (issue #84). Empty for the common acyclic case.
    let forward_map = forward_ref_map(&ir.types, &ir.tag_types);
    let reaching_cycle = types_reaching_cycle(&ir.types, &ir.tag_types);
    let empty_forward = std::collections::HashSet::new();

    // version.py
    let version = render(
        &env,
        "version.py",
        "version.py",
        context! { project_name => ir.project_name },
    )?;
    files.push(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/version.py")),
        contents: version,
    });

    // py.typed (an empty marker file).
    files.push(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/py.typed")),
        contents: String::new(),
    });

    // One file per generated type.
    for decl in &ir.types {
        let forward = forward_map.get(decl.name()).unwrap_or(&empty_forward);
        let file = render_type_decl(
            &env,
            decl,
            RefLoc::RootTypes,
            &tag_map,
            ir.extra_fields,
            forward,
            reaching_cycle.contains(decl.name()),
        )?;
        files.push(GeneratedFile {
            path: PathBuf::from(format!("src/{pkg}/types/{}.py", decl.module())),
            contents: file,
        });
    }

    // Tag-scoped hoisted types (inline request/response bodies), each in its tag's
    // own `types/` package, plus a lazy-loader `__init__.py` per such package.
    let mut tag_type_modules: BTreeMap<&str, Vec<&TypeDecl>> = BTreeMap::new();
    for tt in &ir.tag_types {
        tag_type_modules
            .entry(&tt.module)
            .or_default()
            .push(&tt.decl);
    }
    for (module, decls) in &tag_type_modules {
        for decl in decls {
            let forward = forward_map.get(decl.name()).unwrap_or(&empty_forward);
            let location = if module.is_empty() {
                RefLoc::RootTypes
            } else {
                RefLoc::TagTypes((*module).to_string())
            };
            let file = render_type_decl(
                &env,
                decl,
                location,
                &tag_map,
                ir.extra_fields,
                forward,
                reaching_cycle.contains(decl.name()),
            )?;
            files.push(GeneratedFile {
                path: PathBuf::from(format!("src/{pkg}/{module}/types/{}.py", decl.module())),
                contents: file,
            });
        }
        files.push(tag_types_init_file(&env, pkg, module, decls)?);
    }

    // Fern's static core runtime, emitted verbatim (see assets/README.md), plus
    // the auth-shaped `client_wrapper.py`.
    files.extend(core_files(pkg));
    files.push(client_wrapper_file(
        pkg,
        &ir.project_name,
        &ir.auth,
        &ir.global_headers,
    ));

    // `environment.py`: the server-environment enum, when the document declares
    // `servers`.
    if let Some(environment) = &ir.environment {
        files.push(environment_file(&env, pkg, environment)?);
    }

    // One package marker per endpoint client module. Fern's `__init__.py` here is
    // a comment-only header (four blank lines once stripped) — unless the tag owns
    // hoisted inline types, in which case it is a lazy loader re-exporting them.
    for module in &ir.endpoint_modules {
        if let Some(decls) = tag_type_modules.get(module.as_str()) {
            files.push(tag_pkg_init_file(&env, pkg, module, decls)?);
        } else {
            files.push(GeneratedFile {
                path: PathBuf::from(format!("src/{pkg}/{module}/__init__.py")),
                contents: "\n\n\n\n".to_string(),
            });
        }
    }

    let root_eps: Vec<&Endpoint> = ir
        .endpoints
        .iter()
        .filter(|e| e.module.is_empty())
        .collect();
    let root_emittable = !root_eps.is_empty() && root_eps.iter().all(|e| e.emittable);
    if root_emittable {
        files.push(raw_client_file(&env, pkg, "", &root_eps, &tag_map)?);
    }

    // Per-tag `raw_client.py` and `client.py`, but only for modules whose every
    // operation is within the subset crozier emits today (see
    // `Endpoint::emittable`); the rest await wider endpoint support. The modules
    // that clear this bar (in sorted order) also drive the root client below.
    let mut emittable_modules: Vec<&String> = Vec::new();
    for module in &ir.endpoint_modules {
        let eps: Vec<&Endpoint> = ir
            .endpoints
            .iter()
            .filter(|e| &e.module == module)
            .collect();
        if !eps.is_empty() && eps.iter().all(|e| e.emittable) {
            files.push(raw_client_file(&env, pkg, module, &eps, &tag_map)?);
            let cx = ClientCtx {
                pkg,
                client_name: &ir.client_name,
                module,
                types: &ir.types,
                tag_decls: &ir.tag_types,
                auth: &ir.auth,
                has_environment: ir.environment.is_some(),
                tag_types: &tag_map,
                global_headers: &ir.global_headers,
            };
            files.push(client_file(&env, &cx, &eps)?);
            emittable_modules.push(module);
        }
    }
    // `emittable_modules` stays in first-appearance (declaration) order — the order
    // `ir.endpoint_modules` yields — because that is the order Fern lists sub-clients
    // in the root client and `reference.md` (e.g. `widgets` before `gadgets` when
    // `/widgets` is declared first, not alphabetically). The root `__init__.py`
    // re-sorts its own dynamic-import/`__all__` maps, so its output is unaffected.

    // Root client: `FernApi`/`AsyncFernApi` aggregating the tag clients. Emitted
    // only when there is at least one tag client to aggregate.
    if root_emittable || !emittable_modules.is_empty() {
        files.push(root_client_file(
            &env,
            RootClientFileCtx {
                pkg,
                client_name: &ir.client_name,
                modules: &emittable_modules,
                root_endpoints: if root_emittable { &root_eps } else { &[] },
                types: &ir.types,
                tag_decls: &ir.tag_types,
                tag_map: &tag_map,
                auth: &ir.auth,
                environment: ir.environment.as_ref(),
                global_headers: &ir.global_headers,
            },
        )?);
    }

    // The `errors/` package: one exception class per distinct declared error,
    // plus the lazy-loading `__init__.py` aggregator. Emitted only when there are
    // errors to declare.
    files.extend(error_files(&env, pkg, &ir.errors, &tag_map)?);

    // Package aggregators: `types/__init__.py` over the type layer, and the
    // package-root `__init__.py` re-exporting types, errors, the endpoint
    // submodules, the root client, and `__version__`.
    if !ir.types.is_empty() {
        files.push(types_init_file(&env, pkg, &ir.types)?);
    }
    if root_emittable || !emittable_modules.is_empty() {
        files.push(root_init_file(&env, pkg, ir, &emittable_modules)?);
    }

    // Generated `README.md` (usage examples from the first endpoint) and the
    // per-endpoint `reference.md`.
    if let Some(readme) = readme_file(ir) {
        files.push(readme);
    }
    if let Some(reference) = reference_file(&env, ir, &emittable_modules, &tag_map)? {
        files.push(reference);
    }

    // Project-root scaffolding (pyproject.toml, requirements.txt, metadata).
    files.extend(scaffolding_files(pkg, &ir.project_name));

    // Final wrapping is delegated to `ruff format` (the tool Fern runs), so the
    // emitters above produce content-correct Python without reproducing ruff's
    // line layout. `.py` files are formatted in place; non-Python outputs
    // (Markdown, TOML, JSON, the `py.typed` marker) are left untouched.
    format_python_files(pkg, &mut files)?;

    Ok(files)
}

/// Generate `environment.py`: an `enum.Enum` of the SDK's server environments.
/// Fern's OpenAPI importer emits a single member (see [`ir::Environment`]).
fn environment_file(
    env: &Environment<'static>,
    pkg: &str,
    environment: &crate::ir::Environment,
) -> Result<GeneratedFile> {
    let (member, url) = &environment.member;
    let body = format!(
        "import enum\n\n\nclass {}(enum.Enum):\n    {member} = \"{}\"\n",
        environment.enum_name,
        escape_py_str(url),
    );
    let contents = render(
        env,
        "file.py",
        "environment.py",
        context! { header => HEADER, body => body },
    )?;
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/environment.py")),
        contents,
    })
}

/// Generate the `errors/` package: an exception module per class and the
/// lazy-loading `__init__.py` re-exporting them. Empty when there are no errors.
fn error_files(
    env: &Environment<'static>,
    pkg: &str,
    errors: &[ErrorClass],
    tag_types: &BTreeMap<String, String>,
) -> Result<Vec<GeneratedFile>> {
    if errors.is_empty() {
        return Ok(Vec::new());
    }
    let mut files = Vec::new();
    for err in errors {
        let mut imports = Imports::at(RefLoc::Errors, tag_types);
        imports.add_plain("typing");
        imports.add_from("..core.api_error", "ApiError");
        // Registers the body type's `..types.<module>` import.
        let body_type = raw_type_str(&err.body_type, &mut imports);
        let module = naming::module_name(&err.class_name);
        let contents = render(
            env,
            "error.py",
            &err.class_name,
            context! {
                header => HEADER,
                imports => imports.render(),
                class_name => err.class_name,
                body_type => body_type,
                status_code => err.status_code,
            },
        )?;
        files.push(GeneratedFile {
            path: PathBuf::from(format!("src/{pkg}/errors/{module}.py")),
            contents,
        });
    }
    files.push(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/errors/__init__.py")),
        contents: lazy_loader_init(env, errors)?,
    });
    Ok(files)
}

/// Fern's package-`__init__` lazy loader for the `errors/` package: a
/// `TYPE_CHECKING` import block, a `_dynamic_imports` map, the static
/// `__getattr__`/`__dir__` machinery, and `__all__`. The error classes arrive
/// pre-sorted, so all three sections are alphabetical.
fn lazy_loader_init(env: &Environment<'static>, errors: &[ErrorClass]) -> Result<String> {
    let type_checking: String = errors
        .iter()
        .map(|e| {
            format!(
                "    from .{} import {}\n",
                naming::module_name(&e.class_name),
                e.class_name
            )
        })
        .collect();
    let pairs: Vec<(String, String)> = errors
        .iter()
        .map(|e| {
            (
                e.class_name.clone(),
                format!(".{}", naming::module_name(&e.class_name)),
            )
        })
        .collect();
    let names: Vec<String> = errors.iter().map(|e| e.class_name.clone()).collect();
    render_lazy_loader(env, &type_checking, &pairs, &names)
}

/// Assemble a lazy-loader `__init__.py` from its `TYPE_CHECKING` block, the
/// `_dynamic_imports` pairs `(exported name, ".module")`, and the `__all__`
/// names. The `_dynamic_imports` map and `__all__` are always alphabetical
/// (sorted here); only the `TYPE_CHECKING` block's order is caller-controlled.
/// The header collapses to the four leading blank lines Fern's comment header
/// strips to.
fn render_lazy_loader(
    env: &Environment<'static>,
    type_checking: &str,
    pairs: &[(String, String)],
    names: &[String],
) -> Result<String> {
    let mut pairs = pairs.to_vec();
    pairs.sort();
    let mut names = names.to_vec();
    names.sort();

    // `_dynamic_imports` and `__all__` are emitted flat; `ruff format` wraps them
    // (one entry per line with a trailing comma) when they overflow.
    let entries: Vec<String> = pairs
        .iter()
        .map(|(n, m)| format!("\"{n}\": \"{m}\""))
        .collect();
    let dynamic = format!(
        "_dynamic_imports: typing.Dict[str, str] = {{{}}}",
        entries.join(", ")
    );
    let all_entries: Vec<String> = names.iter().map(|n| format!("\"{n}\"")).collect();
    let all = format!("__all__ = [{}]", all_entries.join(", "));

    render(
        env,
        "lazy_init.py",
        "__init__.py",
        context! {
            header => HEADER,
            type_checking => type_checking,
            dynamic => dynamic,
            all => all,
        },
    )
}

/// The `types/__init__.py` lazy loader. `_dynamic_imports`/`__all__` are
/// alphabetical; the `TYPE_CHECKING` block follows Fern's endpoint-traversal
/// order, which for this corpus is the `Types*` types in reverse declaration
/// order followed by the remaining types alphabetically.
fn types_init_file(
    env: &Environment<'static>,
    pkg: &str,
    types: &[TypeDecl],
) -> Result<GeneratedFile> {
    // Each declaration may export more than its primary name (a discriminated
    // union also exports its per-variant wrappers), all sharing the decl module.
    let mut module_of: std::collections::HashMap<String, String> = std::collections::HashMap::new();
    let mut names: Vec<String> = Vec::new();
    for decl in types {
        for n in decl.exported_names() {
            names.push(n.to_string());
            module_of.insert(n.to_string(), decl.module().to_string());
        }
    }

    // TYPE_CHECKING imports are emitted alphabetically. Their order is never
    // executed and the e2e canonicalizes it with isort, so crozier sorts
    // straightforwardly rather than reproducing Fern's traversal order.
    let mut ordered = names.clone();
    ordered.sort();

    // Consecutive names sharing a module collapse into one `from .mod import a, b`
    // line (Fern groups a discriminated union's names on a single import).
    let module = |n: &str| {
        module_of
            .get(n)
            .cloned()
            .unwrap_or_else(|| naming::module_name(n))
    };
    let mut type_checking = String::new();
    let mut i = 0;
    while i < ordered.len() {
        let m = module(&ordered[i]);
        let mut group = vec![ordered[i].clone()];
        while i + 1 < ordered.len() && module(&ordered[i + 1]) == m {
            i += 1;
            group.push(ordered[i].clone());
        }
        type_checking.push_str(&format!("    from .{m} import {}\n", group.join(", ")));
        i += 1;
    }

    let pairs: Vec<(String, String)> = names
        .iter()
        .map(|n| (n.clone(), format!(".{}", module(n))))
        .collect();
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/types/__init__.py")),
        contents: render_lazy_loader(env, &type_checking, &pairs, &names)?,
    })
}

/// The `{tag}/types/__init__.py` lazy loader over a tag's hoisted inline types.
/// Each type sits in its own module (`.inlined_search_response`); the block is
/// alphabetical (the e2e canonicalizes order with isort).
fn tag_types_init_file(
    env: &Environment<'static>,
    pkg: &str,
    module: &str,
    decls: &[&TypeDecl],
) -> Result<GeneratedFile> {
    let mut named: Vec<(String, String)> = decls
        .iter()
        .flat_map(|d| {
            d.exported_names()
                .into_iter()
                .map(|n| (n.to_string(), d.module().to_string()))
        })
        .collect();
    named.sort();
    let type_checking: String = named
        .iter()
        .map(|(n, m)| format!("    from .{m} import {n}\n"))
        .collect();
    let pairs: Vec<(String, String)> = named
        .iter()
        .map(|(n, m)| (n.clone(), format!(".{m}")))
        .collect();
    let names: Vec<String> = named.iter().map(|(n, _)| n.clone()).collect();
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/{module}/types/__init__.py")),
        contents: render_lazy_loader(env, &type_checking, &pairs, &names)?,
    })
}

/// The tag package's `__init__.py` when it owns hoisted types: a lazy loader
/// re-exporting them from the tag's `.types` subpackage. A tag with no hoisted
/// types keeps the plain comment-only marker instead.
fn tag_pkg_init_file(
    env: &Environment<'static>,
    pkg: &str,
    module: &str,
    decls: &[&TypeDecl],
) -> Result<GeneratedFile> {
    let mut names: Vec<String> = decls
        .iter()
        .flat_map(|d| d.exported_names().into_iter().map(str::to_string))
        .collect();
    names.sort();
    let type_checking = from_import_block(".types", &names, 4);
    let pairs: Vec<(String, String)> = names
        .iter()
        .map(|n| (n.clone(), ".types".to_string()))
        .collect();
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/{module}/__init__.py")),
        contents: render_lazy_loader(env, &type_checking, &pairs, &names)?,
    })
}

/// The package-root `__init__.py` lazy loader, re-exporting the type layer, the
/// errors, the endpoint submodules, the root client, and `__version__`. Every
/// section is alphabetical; the `TYPE_CHECKING` block groups the imports by
/// source (`.types`, `.errors`, submodules, `.client`, `.version`).
fn root_init_file(
    env: &Environment<'static>,
    pkg: &str,
    ir: &Ir,
    modules: &[&String],
) -> Result<GeneratedFile> {
    let client_name = &ir.client_name;
    let async_name = format!("Async{client_name}");

    let mut type_names: Vec<String> = ir
        .types
        .iter()
        .flat_map(|t| t.exported_names())
        .map(str::to_string)
        .collect();
    type_names.sort();
    // Hoisted tag types are re-exported at the package root through their tag
    // submodule (`from .inlined import InlinedSearchResponse`), grouped per tag.
    let mut hoisted_by_tag: BTreeMap<String, Vec<String>> = BTreeMap::new();
    for tt in &ir.tag_types {
        let names = tt.decl.exported_names().into_iter().map(str::to_string);
        if tt.module.is_empty() {
            type_names.extend(names);
        } else {
            hoisted_by_tag
                .entry(tt.module.clone())
                .or_default()
                .extend(names);
        }
    }
    type_names.sort();
    let environment = ir.environment.as_ref();
    let mut error_names: Vec<String> = ir.errors.iter().map(|e| e.class_name.clone()).collect();
    error_names.sort();
    let mut module_names: Vec<String> = modules.iter().map(|m| (*m).clone()).collect();
    module_names.sort();

    // Grouped TYPE_CHECKING block (each group alphabetical, ruff-wrapped at 120).
    let mut tc = String::new();
    tc.push_str(&from_import_block(".types", &type_names, 4));
    tc.push_str(&from_import_block(".errors", &error_names, 4));
    tc.push_str(&from_import_block(".", &module_names, 4));
    tc.push_str(&from_import_block(
        ".client",
        &[async_name.clone(), client_name.to_string()],
        4,
    ));
    if let Some(e) = environment {
        tc.push_str(&from_import_block(
            ".environment",
            std::slice::from_ref(&e.enum_name),
            4,
        ));
    }
    for (tag, names) in &hoisted_by_tag {
        tc.push_str(&from_import_block(&format!(".{tag}"), names, 4));
    }
    tc.push_str(&from_import_block(
        ".version",
        &["__version__".to_string()],
        4,
    ));

    // `_dynamic_imports` pairs: every symbol → its source module.
    let mut pairs: Vec<(String, String)> = Vec::new();
    for n in &type_names {
        pairs.push((n.clone(), ".types".to_string()));
    }
    for n in &error_names {
        pairs.push((n.clone(), ".errors".to_string()));
    }
    for m in &module_names {
        pairs.push((m.clone(), format!(".{m}")));
    }
    pairs.push((client_name.to_string(), ".client".to_string()));
    pairs.push((async_name, ".client".to_string()));
    if let Some(e) = environment {
        pairs.push((e.enum_name.clone(), ".environment".to_string()));
    }
    for (tag, names) in &hoisted_by_tag {
        for n in names {
            pairs.push((n.clone(), format!(".{tag}")));
        }
    }
    pairs.push(("__version__".to_string(), ".version".to_string()));

    let names: Vec<String> = pairs.iter().map(|(n, _)| n.clone()).collect();
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/__init__.py")),
        contents: render_lazy_loader(env, &tc, &pairs, &names)?,
    })
}

/// Whether Fern shows a `...` placeholder for an endpoint's body in the README's
/// abbreviated snippets (versus empty parens). Empirically `...` appears for a
/// "non-trivial" body: a container body, or an inline object that either carries
/// an object/union-typed field (one serialized through the convert wrapper, e.g.
/// `inline`'s `filter`) or has two-or-more fields all required (`gettoken`). A body
/// of only scalar fields with any optional one (`createaccount`) or a single field
/// (`create`), or a union/enum/scalar/no body, renders empty parens.
fn complex_body(ep: &Endpoint, types: &[TypeDecl], tag_decls: &[TagTypeDecl]) -> bool {
    if ep.body_all_of
        || (ep.body_schema_ref
            && ep.body_schema_has_example
            && matches!(ep.request_body, Some(RequestBody::Inline(_))))
    {
        return true;
    }
    match &ep.request_body {
        None => false,
        Some(RequestBody::Bytes { .. }) => true,
        Some(RequestBody::Inline(fields)) => {
            fields.iter().any(|f| f.convert)
                || (fields.len() >= 2 && fields.iter().all(|f| f.spec_required))
        }
        Some(RequestBody::Form(form)) => {
            ep.module.is_empty()
                || form.fields.iter().any(|f| f.is_file)
                || form.fields.iter().all(|f| f.spec_required)
        }
        Some(RequestBody::Single(s)) => is_complex_type(&s.type_ref, types, tag_decls),
    }
}

/// Whether an object rendered as a worked-example body is "complex" (`...`) rather
/// than empty parens: two-or-more fields all required. A single field, or any
/// optional field, renders `()` — the same evidence-based split as an inline body.
fn object_body_complex(obj: &ObjectType) -> bool {
    obj.fields.len() >= 2 && obj.fields.iter().all(|f| f.spec_required)
}

/// The object an example-body container holds, if any: unwrap optionals and resolve
/// a `Named` to a package-root or hoisted tag object.
fn container_element_object<'a>(
    t: &TypeRef,
    types: &'a [TypeDecl],
    tag_decls: &'a [TagTypeDecl],
) -> Option<&'a ObjectType> {
    match t {
        TypeRef::Optional(inner) => container_element_object(inner, types, tag_decls),
        TypeRef::Named(n) => {
            let decl = types.iter().find(|d| d.name() == n).or_else(|| {
                tag_decls
                    .iter()
                    .find_map(|tt| (tt.decl.name() == n).then_some(&tt.decl))
            })?;
            match decl {
                TypeDecl::Object(o) => Some(o),
                _ => None,
            }
        }
        _ => None,
    }
}

/// Whether a single-argument body type renders as `...` in the abbreviated snippets
/// (versus empty parens). A container of objects defers to its element's shape — an
/// element with an optional field renders `()`, matching Fern; a container of
/// scalars stays complex. A plain object is complex; a union/enum/scalar is not.
fn is_complex_type(t: &TypeRef, types: &[TypeDecl], tag_decls: &[TagTypeDecl]) -> bool {
    match t {
        TypeRef::List(inner) | TypeRef::Set(inner) => {
            match container_element_object(inner, types, tag_decls) {
                Some(obj) => object_body_complex(obj),
                None => true,
            }
        }
        TypeRef::Dict(..) => true,
        TypeRef::Optional(inner) => is_complex_type(inner, types, tag_decls),
        TypeRef::Named(n) => types
            .iter()
            .find(|d| d.name() == n)
            .map(|d| (d, false))
            .or_else(|| {
                tag_decls
                    .iter()
                    .find(|decl| decl.decl.name() == n)
                    .map(|decl| (&decl.decl, true))
            })
            .is_some_and(|(d, tag_scoped)| match d {
                TypeDecl::Object(_) => true,
                // A union (named or discriminated) renders empty parens; an enum
                // is a single member-access value, likewise not "complex".
                TypeDecl::DiscriminatedUnion(_) | TypeDecl::Enum(_) => false,
                TypeDecl::Alias(a) => {
                    (tag_scoped && matches!(a.target, TypeRef::Union(_)))
                        || (!matches!(a.target, TypeRef::Union(_))
                            && is_complex_type(&a.target, types, tag_decls))
                }
            }),
        TypeRef::Union(_) | TypeRef::Primitive(_) | TypeRef::Literal(_) => false,
    }
}

/// Render an abbreviated call `<prefix>(...)` for the README snippets: empty parens
/// when the body is not complex, else a `...` placeholder wrapped onto its own line
/// when the flat form overflows. Fern's snippet writer targets an 80-column width
/// here (not the project's ruff `line-length`), so bunq's 83-column raw-response call
/// wraps while its 58-column error call stays flat.
fn abbrev_call(indent: usize, prefix: &str, complex: bool) -> String {
    let pad = " ".repeat(indent);
    if !complex {
        return format!("{pad}{prefix}()");
    }
    let flat = format!("{pad}{prefix}(...)");
    if flat.len() <= 80 {
        flat
    } else {
        format!("{pad}{prefix}(\n{}...\n{pad})", " ".repeat(indent + 4))
    }
}

fn select_readme_endpoint<'a>(
    endpoints: impl Clone + Iterator<Item = &'a Endpoint>,
) -> Option<&'a Endpoint> {
    endpoints
        .clone()
        .find(|e| {
            e.emittable
                && e.http_method == "POST"
                && !matches!(e.request_body, Some(RequestBody::Bytes { .. }))
        })
        .or_else(|| {
            endpoints.clone().find(|e| {
                e.emittable
                    && e.request_body.is_some()
                    && !matches!(e.request_body, Some(RequestBody::Bytes { .. }))
            })
        })
        .or_else(|| {
            endpoints.clone().find(|e| {
                e.emittable
                    && e.http_method != "GET"
                    && e.path_params.is_empty()
                    && e.query_params.is_empty()
                    && e.header_params.is_empty()
                    && e.request_body.is_none()
            })
        })
        .or_else(|| {
            endpoints
                .clone()
                .find(|e| e.emittable && !matches!(e.request_body, Some(RequestBody::Bytes { .. })))
        })
}

fn readme_endpoint(ir: &Ir) -> Option<&Endpoint> {
    if ir.openapi_31 {
        return select_readme_endpoint(ir.endpoints.iter());
    }
    select_readme_endpoint(ir.endpoints.iter().filter(|e| e.module.is_empty()))
        .filter(|e| e.http_method != "GET" || e.request_body.is_some())
        .or_else(|| select_readme_endpoint(ir.endpoints.iter()))
}

fn client_call_prefix(ep: &Endpoint) -> String {
    if ep.module.is_empty() {
        format!("client.{}", ep.method_name)
    } else {
        format!("client.{}.{}", ep.module, ep.method_name)
    }
}

fn raw_client_call_prefix(ep: &Endpoint) -> String {
    if ep.module.is_empty() {
        format!("response = client.with_raw_response.{}", ep.method_name)
    } else {
        format!(
            "response = client.{}.with_raw_response.{}",
            ep.module, ep.method_name
        )
    }
}

/// The generated `README.md`: mostly static prose with the SDK name/package
/// substituted and a worked usage example (sync + async) synthesized from the
/// first endpoint. Compared verbatim (README is not comment-stripped). Emitted
/// only when there is an endpoint to demonstrate.
fn readme_file(ir: &Ir) -> Option<GeneratedFile> {
    // Fern demonstrates the first emittable endpoint that has a request body (the
    // most illustrative call). APIs with no body-bearing operations use the first
    // no-argument non-GET operation before falling back to the first endpoint.
    let first = readme_endpoint(ir)?;
    let pkg = &ir.package_name;
    let org = naming::to_pascal_case(pkg);
    let async_name = format!("Async{}", ir.client_name);

    // The abbreviated calls in the error-handling and advanced sections show `...`
    // for an endpoint with a complex (object/container/union) body, else empty
    // parens; the error/raw-response calls are ruff-wrapped at the snippet width 88.
    let complex = (first.request_body.is_none()
        && (first.http_method != "GET" || matches!(first.response, Some(TypeRef::Dict(..)))))
        || (ir.openapi_31
            && first.body_schema_implicit_object
            && matches!(
                &first.request_body,
                Some(RequestBody::Inline(fields)) if fields.iter().all(|field| field.spec_required)
            ))
        || complex_body(first, &ir.types, &ir.tag_types);
    let err_call = abbrev_call(4, &client_call_prefix(first), complex);
    let raw_call = abbrev_call(0, &raw_client_call_prefix(first), complex);
    let retry_prefix = client_call_prefix(first);
    let retry_call = format!(
        "{retry_prefix}({}request_options={{",
        if complex { "..., " } else { "" }
    );

    let sync_example = {
        let mut ctx = ExampleCtx {
            types: &ir.types,
            tag_decls: &ir.tag_types,
            referenced: BTreeSet::new(),
            referenced_tag: BTreeSet::new(),
            uses_datetime: false,
            auth: &ir.auth,
            has_environment: ir.environment.is_some(),
            global_headers: &ir.global_headers,
            building: Default::default(),
        };
        build_example(first, false, &first.module, pkg, &ir.client_name, &mut ctx)?.join("\n")
    };
    let async_example = {
        let mut ctx = ExampleCtx {
            types: &ir.types,
            tag_decls: &ir.tag_types,
            referenced: BTreeSet::new(),
            referenced_tag: BTreeSet::new(),
            uses_datetime: false,
            auth: &ir.auth,
            has_environment: ir.environment.is_some(),
            global_headers: &ir.global_headers,
            building: Default::default(),
        };
        build_example(first, true, &first.module, pkg, &ir.client_name, &mut ctx)?.join("\n")
    };

    let contents = include_str!("../assets/scaffolding/README.md.tmpl")
        .replace("@@ORG@@", &org)
        .replace("@@PROJECT@@", &ir.project_name)
        .replace("@@PKG@@", pkg)
        .replace("@@CLIENT@@", &ir.client_name)
        .replace("@@ASYNC@@", &async_name)
        .replace("@@ERR_CALL@@", &err_call)
        .replace("@@RAW_CALL@@", &raw_call)
        .replace("@@RETRY_CALL@@", &retry_call)
        .replace("@@USAGE@@", &sync_example)
        .replace("@@ASYNC_EXAMPLE@@", &async_example);
    Some(GeneratedFile {
        path: PathBuf::from("README.md"),
        contents,
    })
}

/// The generated `reference.md`: a per-endpoint reference grouped by tag, each
/// entry a collapsible `<details>` with an optional description, a worked sync
/// usage example, and a parameter table. Endpoints without an example (a raw
/// bytes body) are omitted, exactly as Fern does. Compared verbatim.
fn reference_file(
    env: &Environment<'static>,
    ir: &Ir,
    modules: &[&String],
    tag_types: &BTreeMap<String, String>,
) -> Result<Option<GeneratedFile>> {
    let pkg = &ir.package_name;
    let mut blocks: Vec<String> = vec!["# Reference".to_string()];
    let mut any = false;

    let root_eps: Vec<&Endpoint> = ir
        .endpoints
        .iter()
        .filter(|e| e.module.is_empty() && e.emittable)
        .filter(|e| !matches!(e.request_body, Some(RequestBody::Bytes { .. })))
        .filter(|e| endpoint_has_worked_example(e))
        .collect();
    for ep in root_eps {
        any = true;
        blocks.push(reference_entry(env, ir, ep, &ep.module, pkg, tag_types)?);
        blocks.push(String::new());
    }

    for module in modules {
        let eps: Vec<&Endpoint> = ir
            .endpoints
            .iter()
            .filter(|e| &e.module == *module && e.emittable)
            .filter(|e| !matches!(e.request_body, Some(RequestBody::Bytes { .. })))
            .filter(|e| endpoint_has_worked_example(e))
            .collect();
        any = true;
        let title = ir
            .endpoint_module_titles
            .get(*module)
            .cloned()
            .unwrap_or_else(|| naming::to_pascal_case(module));
        blocks.push(format!("## {title}"));
        for ep in eps {
            blocks.push(reference_entry(env, ir, ep, module, pkg, tag_types)?);
            blocks.push(String::new());
        }
    }
    if !any {
        return Ok(None);
    }
    // The last entry's trailing blank line is kept: Fern ends the file with one.
    Ok(Some(GeneratedFile {
        path: PathBuf::from("reference.md"),
        contents: format!("{}\n", blocks.join("\n")),
    }))
}

/// One parameter row in the `reference.md` table: name, annotation, and the
/// suffix (a lone space, or ` — <description>`).
#[derive(Serialize)]
struct ParamRow {
    name: String,
    annot: String,
    suffix: String,
}

fn reference_param_suffix(description: Option<&str>) -> String {
    match description {
        Some(d) if d.contains('\n') => format!(" \n\n{d}"),
        Some(d) => format!(" — {d}"),
        None => " ".to_string(),
    }
}

/// The view model for one `reference.md` endpoint entry (`reference_entry.md.j2`).
#[derive(Serialize)]
struct ReferenceEntryView {
    client_prefix: String,
    href: String,
    pkg: String,
    method: String,
    dots: &'static str,
    description: Option<String>,
    example: String,
    params: Vec<ParamRow>,
}

/// Render one `reference.md` endpoint entry (a `<details>` block), returning it
/// without a trailing newline so [`reference_file`] can join entries uniformly.
fn reference_entry(
    env: &Environment<'static>,
    ir: &Ir,
    ep: &Endpoint,
    module: &str,
    pkg: &str,
    tag_types: &BTreeMap<String, String>,
) -> Result<String> {
    let mut imports = Imports::at(RefLoc::Client(module.to_string()), tag_types);
    let mp = method_params(ep, &mut imports);

    // The example (sync form). Bytes bodies are filtered out before this point.
    let mut ctx = ExampleCtx {
        types: &ir.types,
        tag_decls: &ir.tag_types,
        referenced: BTreeSet::new(),
        referenced_tag: BTreeSet::new(),
        uses_datetime: false,
        auth: &ir.auth,
        has_environment: ir.environment.is_some(),
        global_headers: &ir.global_headers,
        building: Default::default(),
    };
    let example = build_example(ep, false, module, pkg, &ir.client_name, &mut ctx)
        .unwrap_or_default()
        .join("\n");

    // The parameter rows, in signature order, then `request_options`.
    let mut params: Vec<ParamRow> = Vec::new();
    for (name, ty, desc) in &mp.path {
        // A *declared* description — even the empty string — gets the ` — {desc}`
        // separator (Fern emits the em-dash whenever the parameter carries a
        // `description` key); only an omitted (`None`) one gets the bare space.
        let suffix = reference_param_suffix(desc.as_deref());
        params.push(ParamRow {
            name: name.clone(),
            annot: ty.clone(),
            suffix,
        });
    }
    for dp in ordered_keyword_params(&mp.query, &mp.header, &mp.body) {
        let suffix = reference_param_suffix(dp.description.as_deref());
        let annot = reference_param_annotation(&dp.annotation);
        params.push(ParamRow {
            name: dp.name.clone(),
            annot,
            suffix,
        });
    }
    let request_options_suffix = if ep.binary_response {
        " — Request-specific configuration. You can pass in configuration such as `chunk_size`, and more to customize the request and response."
    } else {
        " — Request-specific configuration."
    };
    params.push(ParamRow {
        name: "request_options".to_string(),
        annot: "typing.Optional[RequestOptions]".to_string(),
        suffix: request_options_suffix.to_string(),
    });

    let has_args =
        !mp.path.is_empty() || !mp.query.is_empty() || !mp.header.is_empty() || !mp.body.is_empty();
    let view = ReferenceEntryView {
        client_prefix: if module.is_empty() {
            "client.".to_string()
        } else {
            format!("client.{module}.")
        },
        href: if module.is_empty() {
            format!("src/{pkg}/client.py")
        } else {
            format!("src/{pkg}/{module}/client.py")
        },
        pkg: pkg.to_string(),
        method: ep.method_name.clone(),
        dots: if has_args { "..." } else { "" },
        description: ep.docstring.as_ref().map(|description| {
            if ep.reference_description_trailing_blank {
                format!("{description}\n")
            } else {
                description.clone()
            }
        }),
        example,
        params,
    };
    let rendered = render(
        env,
        "reference_entry.md",
        "reference.md",
        minijinja::Value::from_serialize(&view),
    )?;
    Ok(rendered.trim_end_matches('\n').to_string())
}

fn reference_param_annotation(annotation: &str) -> String {
    // Fern wraps the generated scalar-or-sequence type for array query params in
    // reference tables, while leaving the method signature on one line.
    if let Some(inner) = annotation
        .strip_prefix("typing.Optional[typing.Union[")
        .and_then(|s| s.strip_suffix("]]"))
    {
        if let Some((item, sequence_item)) = inner.split_once(", typing.Sequence[") {
            if sequence_item.strip_suffix(']') == Some(item)
                && item.chars().next().is_some_and(char::is_uppercase)
            {
                return format!(
                    "typing.Optional[\n    typing.Union[\n        {item},\n        typing.Sequence[{item}],\n    ]\n]"
                );
            }
        }
    }

    // Fern renders a `core.File` type in the reference parameter table with a
    // leading `from __future__ import annotations` (an artifact of how it prints
    // the forward-referenced file type); reproduce it so the table matches.
    if annotation == "core.File" {
        "from __future__ import annotations\n\ncore.File".to_string()
    } else {
        annotation.to_string()
    }
}

/// Render `from <module> import <names>` at `indent` spaces, sorted, on one line.
/// `ruff format` wraps it into a parenthesized per-name block if it overflows.
/// Returns the statement with its trailing newline.
fn from_import_block(module: &str, names: &[String], indent: usize) -> String {
    // An empty group emits nothing — a `from <module> import` with no names is a
    // syntax error, and the source module (e.g. `errors/`) is not emitted when
    // there is nothing to import from it. The corpus always has errors, so this
    // path is only reached for specs that declare none.
    if names.is_empty() {
        return String::new();
    }
    let pad = " ".repeat(indent);
    let mut names = names.to_vec();
    names.sort();
    format!("{pad}from {module} import {}\n", names.join(", "))
}

