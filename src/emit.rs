/// Placeholders substituted in `client_wrapper.py`.
const SDK_NAME_PLACEHOLDER: &str = "@@CROZIER_SDK_NAME@@";
const SDK_VERSION_PLACEHOLDER: &str = "@@CROZIER_SDK_VERSION@@";
/// Default SDK version stamped into the runtime (Fern uses `0.0.0` when none is
/// configured). Not yet exposed as a flag.
const DEFAULT_SDK_VERSION: &str = "0.0.0";

/// Emit the vendored core runtime for a package, substituting the SDK name
/// (project name) and version into `client_wrapper.py`.
fn core_files(pkg: &str, project_name: &str) -> Vec<GeneratedFile> {
    CORE_ASSETS
        .iter()
        .map(|(rel, content)| GeneratedFile {
            path: PathBuf::from(format!("src/{pkg}/core/{rel}")),
            contents: content
                .replace(SDK_NAME_PLACEHOLDER, project_name)
                .replace(SDK_VERSION_PLACEHOLDER, DEFAULT_SDK_VERSION),
        })
        .collect()
}

/// The pydantic model configuration block Fern emits at the end of every model.
/// Static (the `# type: ignore` comment is stripped before the byte comparison).
const CONFIG_BLOCK: &str = "    if IS_PYDANTIC_V2:
        model_config: typing.ClassVar[pydantic.ConfigDict] = pydantic.ConfigDict(extra=\"allow\", frozen=True)  # type: ignore # Pydantic v2
    else:

        class Config:
            frozen = True
            smart_union = True
            extra = pydantic.Extra.allow";

/// Render a docstring as an indented triple-quoted block, escaping backslashes
/// the way Fern does so the value round-trips. Every line (including blank ones)
/// carries the block indent; the block has no trailing newline.
fn render_docstring(doc: &str, indent: usize) -> String {
    let pad = " ".repeat(indent);
    let mut out = format!("{pad}\"\"\"\n");
    for line in doc.split('\n') {
        out.push_str(&pad);
        out.push_str(&line.replace('\\', "\\\\"));
        out.push('\n');
    }
    out.push_str(&pad);
    out.push_str("\"\"\"");
    out
}

/// Assemble a model's class body: an optional class docstring, the fields (each
/// with an optional docstring), then the config block. Consecutive fields are
/// separated by a blank line only after a documented field; a blank line always
/// precedes the config block. No trailing newline (the template adds it).
fn object_body(class_docstring: Option<&str>, fields: &[RenderedField]) -> String {
    let mut body = String::new();
    if let Some(doc) = class_docstring {
        body.push_str(&render_docstring(doc, 4));
        body.push_str("\n\n");
    }
    for (i, field) in fields.iter().enumerate() {
        let line = wrap::layout(
            &format!("    {}: ", field.py_name),
            &field.annotation,
            &field.default,
            4,
        );
        body.push_str(&line);
        body.push('\n');
        if let Some(doc) = &field.docstring {
            body.push_str(&render_docstring(doc, 4));
            body.push('\n');
        }
        // Separate a documented field from the next field with a blank line.
        if field.docstring.is_some() && i + 1 < fields.len() {
            body.push('\n');
        }
    }
    if !fields.is_empty() {
        body.push('\n');
    }
    body.push_str(CONFIG_BLOCK);
    body
}

/// Render one type declaration to a file body.
fn render_type_decl(env: &Environment<'static>, decl: &TypeDecl) -> Result<String> {
    match decl {
        TypeDecl::Object(obj) => {
            let mut imports = Imports::default();
            imports.add_plain("typing");
            imports.add_plain("pydantic");
            imports.add_from("..core.pydantic_utilities", "IS_PYDANTIC_V2");
            // Bases come from an `allOf`'s `$ref` members; with none, the model
            // extends Fern's `UniversalBaseModel`.
            let bases = if obj.bases.is_empty() {
                imports.add_from("..core.pydantic_utilities", "UniversalBaseModel");
                "UniversalBaseModel".to_string()
            } else {
                for base in &obj.bases {
                    imports.add_from(&format!(".{}", naming::module_name(base)), base);
                }
                obj.bases.join(", ")
            };
            let fields: Vec<RenderedField> = obj
                .fields
                .iter()
                .map(|f| render_field(f, &mut imports))
                .collect();
            render(
                env,
                "object.py",
                &obj.name,
                context! {
                    header => HEADER,
                    imports => imports.render(),
                    class_name => obj.name,
                    bases => bases,
                    body => object_body(obj.docstring.as_deref(), &fields),
                },
            )
        }
        TypeDecl::Alias(alias) => {
            let mut imports = Imports::default();
            let target = render_type(&alias.target, &mut imports);
            let assignment = wrap::layout(&format!("{} = ", alias.name), &target, "", 0);
            render(
                env,
                "alias.py",
                &alias.name,
                context! {
                    header => HEADER,
                    imports => imports.render(),
                    assignment => assignment,
                },
            )
        }
    }
}

/// Render a resolved type to its flat Python expression for the raw client,
/// registering imports relative to the package root (named types live under
/// `..types`). Only the shapes reachable in an emittable endpoint are exercised;
/// the rest are rendered structurally for completeness.
fn raw_type_str(t: &TypeRef, imports: &mut Imports) -> String {
    match t {
        TypeRef::Primitive(Prim::Str) => "str".to_string(),
        TypeRef::Primitive(Prim::Int) => "int".to_string(),
        TypeRef::Primitive(Prim::Float) => "float".to_string(),
        TypeRef::Primitive(Prim::Bool) => "bool".to_string(),
        TypeRef::Primitive(Prim::Any) => {
            imports.add_plain("typing");
            "typing.Any".to_string()
        }
        TypeRef::Primitive(Prim::Datetime) => {
            imports.add_plain_as("datetime", "dt");
            "dt.datetime".to_string()
        }
        TypeRef::Primitive(Prim::Date) => {
            imports.add_plain_as("datetime", "dt");
            "dt.date".to_string()
        }
        TypeRef::Named(class) => {
            imports.add_from(&format!("..types.{}", naming::module_name(class)), class);
            class.clone()
        }
        TypeRef::Optional(inner) => {
            imports.add_plain("typing");
            format!("typing.Optional[{}]", raw_type_str(inner, imports))
        }
        TypeRef::List(inner) => {
            imports.add_plain("typing");
            format!("typing.List[{}]", raw_type_str(inner, imports))
        }
        TypeRef::Set(inner) => {
            imports.add_plain("typing");
            format!("typing.Set[{}]", raw_type_str(inner, imports))
        }
        TypeRef::Dict(k, v) => {
            imports.add_plain("typing");
            format!(
                "typing.Dict[{}, {}]",
                raw_type_str(k, imports),
                raw_type_str(v, imports)
            )
        }
        TypeRef::Union(variants) => {
            imports.add_plain("typing");
            let inner = variants
                .iter()
                .map(|v| raw_type_str(v, imports))
                .collect::<Vec<_>>()
                .join(", ");
            format!("typing.Union[{inner}]")
        }
        TypeRef::Literal(values) => {
            imports.add_plain("typing");
            let inner = values
                .iter()
                .map(|v| format!("\"{v}\""))
                .collect::<Vec<_>>()
                .join(", ");
            format!("typing.Literal[{inner}]")
        }
    }
}

/// The request's URL argument: the document path with its leading slash stripped,
/// rendered as an f-string interpolating `jsonable_encoder(param)` for each path
/// placeholder, or a plain string literal when there are none.
fn url_arg(ep: &Endpoint) -> String {
    let stripped = ep.path.strip_prefix('/').unwrap_or(&ep.path);
    if stripped.contains('{') {
        let mut rendered = stripped.to_string();
        for pp in &ep.path_params {
            rendered = rendered.replace(
                &format!("{{{}}}", pp.wire_name),
                &format!("{{jsonable_encoder({})}}", pp.py_name),
            );
        }
        format!("f\"{rendered}\"")
    } else {
        format!("\"{stripped}\"")
    }
}

/// Build one raw-client method (sync or async), registering its imports.
fn raw_method(ep: &Endpoint, is_async: bool, imports: &mut Imports) -> String {
    imports.add_plain("typing");
    imports.add_from("..core.request_options", "RequestOptions");

    let wrapper = if is_async {
        "AsyncHttpResponse"
    } else {
        "HttpResponse"
    };
    let inner = ep
        .response
        .as_ref()
        .map_or_else(|| "None".to_string(), |t| raw_type_str(t, imports));
    let return_type = format!("{wrapper}[{inner}]");

    // Path-parameter annotations (registers any imports they need).
    let param_types: Vec<(String, String)> = ep
        .path_params
        .iter()
        .map(|pp| (pp.py_name.clone(), raw_type_str(&pp.type_ref, imports)))
        .collect();

    // Query parameters become keyword-only arguments; each carries the type used
    // in its annotation and docstring (`Optional[..]` unless required) and its
    // description.
    let query_params: Vec<DocParam> = ep
        .query_params
        .iter()
        .map(|qp| {
            let base = raw_type_str(&qp.type_ref, imports);
            let doc_type = if qp.required {
                base
            } else {
                format!("typing.Optional[{base}]")
            };
            DocParam {
                name: qp.py_name.clone(),
                doc_type,
                required: qp.required,
                description: qp.docstring.clone(),
            }
        })
        .collect();

    // The request body renders as a keyword-only `request` argument, carrying the
    // type used in its annotation and docstring (`Optional[..]` unless required).
    let request_param = ep.request_body.as_ref().map(|rb| {
        let base = raw_type_str(&rb.type_ref, imports);
        let doc_type = if rb.required {
            base
        } else {
            format!("typing.Optional[{base}]")
        };
        DocParam {
            name: "request".to_string(),
            doc_type,
            required: rb.required,
            description: None,
        }
    });

    // Signature: `self`, positional path params, `*`, keyword-only query params,
    // the request body, then `request_options`. Laid out with ruff's
    // right-hand-split.
    let mut args: Vec<Doc> = vec![Doc::atom("self")];
    for (name, ty) in &param_types {
        args.push(Doc::atom(format!("{name}: {ty}")));
    }
    args.push(Doc::atom("*"));
    for qp in &query_params {
        args.push(Doc::atom(if qp.required {
            format!("{}: {}", qp.name, qp.doc_type)
        } else {
            format!("{}: {} = None", qp.name, qp.doc_type)
        }));
    }
    if let Some(rb) = &request_param {
        args.push(Doc::atom(if rb.required {
            format!("{}: {}", rb.name, rb.doc_type)
        } else {
            format!("{}: {} = None", rb.name, rb.doc_type)
        }));
    }
    args.push(Doc::atom(
        "request_options: typing.Optional[RequestOptions] = None",
    ));
    let sig_open = format!(
        "    {}def {}(",
        if is_async { "async " } else { "" },
        ep.method_name
    );
    let signature = wrap::layout(
        "",
        &Doc::group(sig_open, args, ")"),
        &format!(" -> {return_type}:"),
        4,
    );

    let docstring = raw_docstring(
        ep,
        &param_types,
        &query_params,
        request_param.as_ref(),
        &return_type,
    );
    let body = raw_body(ep, is_async, &inner, imports);
    format!("{signature}\n{docstring}\n{body}")
}

/// A parameter as it appears in a method docstring / signature: its Python name,
/// the type shown in the annotation and docstring, whether it is required, and an
/// optional description.
struct DocParam {
    name: String,
    doc_type: String,
    required: bool,
    description: Option<String>,
}

/// The method docstring (indent 8): an optional summary line, a `Parameters`
/// section (path params, query params, the request body, then `request_options`),
/// and a `Returns` section.
fn raw_docstring(
    ep: &Endpoint,
    param_types: &[(String, String)],
    query_params: &[DocParam],
    request_param: Option<&DocParam>,
    return_type: &str,
) -> String {
    let mut lines: Vec<String> = vec!["        \"\"\"".to_string()];
    if let Some(summary) = &ep.docstring {
        lines.push(format!("        {summary}"));
        lines.push(String::new());
    }
    lines.push("        Parameters".to_string());
    lines.push("        ----------".to_string());
    for (name, ty) in param_types {
        lines.push(format!("        {name} : {ty}"));
        lines.push(String::new());
    }
    for qp in query_params {
        lines.push(format!("        {} : {}", qp.name, qp.doc_type));
        if let Some(desc) = &qp.description {
            lines.push(format!("            {desc}"));
        }
        lines.push(String::new());
    }
    if let Some(rb) = request_param {
        lines.push(format!("        {} : {}", rb.name, rb.doc_type));
        if let Some(desc) = &rb.description {
            lines.push(format!("            {desc}"));
        }
        lines.push(String::new());
    }
    lines.push("        request_options : typing.Optional[RequestOptions]".to_string());
    lines.push("            Request-specific configuration.".to_string());
    lines.push(String::new());
    lines.push("        Returns".to_string());
    lines.push("        -------".to_string());
    lines.push(format!("        {return_type}"));
    // A concrete response type is followed by a blank line before the closing
    // quotes; a `None` return closes immediately.
    if ep.response.is_some() {
        lines.push(String::new());
    }
    lines.push("        \"\"\"".to_string());
    lines.join("\n")
}

/// The method body (indent 8): the `httpx_client.request(...)` call and the
/// response-handling block.
fn raw_body(ep: &Endpoint, is_async: bool, inner: &str, imports: &mut Imports) -> String {
    imports.add_plain("typing");
    imports.add_from("json.decoder", "JSONDecodeError");
    imports.add_from("..core.api_error", "ApiError");
    if !ep.path_params.is_empty() {
        imports.add_from("..core.jsonable_encoder", "jsonable_encoder");
    }

    let await_ = if is_async { "await " } else { "" };
    let wrapper = if is_async {
        "AsyncHttpResponse"
    } else {
        "HttpResponse"
    };
    let mut lines: Vec<String> = vec![
        format!("        _response = {await_}self._client_wrapper.httpx_client.request("),
        format!("            {},", url_arg(ep)),
        format!("            method=\"{}\",", ep.http_method),
    ];
    // Query parameters map wire name to the Python argument in a `params` dict.
    if !ep.query_params.is_empty() {
        lines.push("            params={".to_string());
        for qp in &ep.query_params {
            lines.push(format!(
                "                \"{}\": {},",
                qp.wire_name, qp.py_name
            ));
        }
        lines.push("            },".to_string());
    }
    // The request body serializes as `json=request`; named (`$ref`) bodies also
    // carry a `content-type: application/json` header.
    if let Some(rb) = &ep.request_body {
        lines.push("            json=request,".to_string());
        if rb.content_type_header {
            lines.extend([
                "            headers={".to_string(),
                "                \"content-type\": \"application/json\",".to_string(),
                "            },".to_string(),
            ]);
        }
    }
    lines.push("            request_options=request_options,".to_string());
    // A request body passes the `OMIT` sentinel so unset optionals drop out.
    if ep.request_body.is_some() {
        lines.push("            omit=OMIT,".to_string());
    }
    lines.extend([
        "        )".to_string(),
        "        try:".to_string(),
        "            if 200 <= _response.status_code < 300:".to_string(),
    ]);
    if ep.response.is_some() {
        imports.add_from("..core.pydantic_utilities", "parse_obj_as");
        lines.extend([
            "                _data = typing.cast(".to_string(),
            format!("                    {inner},"),
            "                    parse_obj_as(".to_string(),
            format!("                        type_={inner},"),
            "                        object_=_response.json(),".to_string(),
            "                    ),".to_string(),
            "                )".to_string(),
            format!("                return {wrapper}(response=_response, data=_data)"),
        ]);
    } else {
        lines.push(format!(
            "                return {wrapper}(response=_response, data=None)"
        ));
    }
    lines.extend([
        "            _response_json = _response.json()".to_string(),
        "        except JSONDecodeError:".to_string(),
        "            raise ApiError(status_code=_response.status_code, headers=dict(_response.headers), body=_response.text)".to_string(),
        "        raise ApiError(status_code=_response.status_code, headers=dict(_response.headers), body=_response_json)".to_string(),
    ]);
    lines.join("\n")
}

/// Assemble the `raw_client.py` file for one client module: the sync and async
/// raw client classes, each wrapping every operation.
fn raw_client_file(
    env: &Environment<'static>,
    pkg: &str,
    module: &str,
    endpoints: &[&Endpoint],
) -> Result<GeneratedFile> {
    let mut imports = Imports::default();
    // Imports every raw client needs regardless of operation shape.
    imports.add_plain("typing");
    imports.add_from("json.decoder", "JSONDecodeError");
    imports.add_from("..core.api_error", "ApiError");
    imports.add_from("..core.client_wrapper", "AsyncClientWrapper");
    imports.add_from("..core.client_wrapper", "SyncClientWrapper");
    imports.add_from("..core.http_response", "AsyncHttpResponse");
    imports.add_from("..core.http_response", "HttpResponse");
    imports.add_from("..core.request_options", "RequestOptions");

    let class_stem = naming::to_pascal_case(module);
    let sync = raw_client_class(
        &format!("Raw{class_stem}Client"),
        "SyncClientWrapper",
        endpoints,
        false,
        &mut imports,
    );
    let async_class = raw_client_class(
        &format!("AsyncRaw{class_stem}Client"),
        "AsyncClientWrapper",
        endpoints,
        true,
        &mut imports,
    );
    // A module with any request body declares the `OMIT` sentinel above the
    // classes; `omit=OMIT` drops unset optional fields from the serialized body.
    let omit = if endpoints.iter().any(|e| e.request_body.is_some()) {
        "OMIT = typing.cast(typing.Any, ...)\n\n\n"
    } else {
        ""
    };
    let body = format!("{omit}{sync}\n\n\n{async_class}");

    let contents = render(
        env,
        "raw_client.py",
        module,
        context! {
            header => HEADER,
            imports => imports.render(),
            body => body,
        },
    )?;
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/{module}/raw_client.py")),
        contents,
    })
}

/// Render one raw-client class: the `__init__` binding the client wrapper, then
/// one method per operation separated by a blank line.
fn raw_client_class(
    class_name: &str,
    wrapper_type: &str,
    endpoints: &[&Endpoint],
    is_async: bool,
    imports: &mut Imports,
) -> String {
    let mut out = format!(
        "class {class_name}:\n    def __init__(self, *, client_wrapper: {wrapper_type}):\n        self._client_wrapper = client_wrapper"
    );
    for ep in endpoints {
        out.push_str("\n\n");
        out.push_str(&raw_method(ep, is_async, imports));
    }
    out
}

/// Render a named template with a context, mapping errors to [`Error::Render`].
fn render(
    env: &Environment<'static>,
    template: &str,
    file: &str,
    ctx: minijinja::value::Value,
) -> Result<String> {
    env.get_template(template)
        .and_then(|t| t.render(ctx))
        .map_err(|e| Error::Render {
            file: file.to_string(),
            message: e.to_string(),
        })
}

/// Write generated files under `root`, creating parent directories.
pub fn write_files(root: &std::path::Path, files: &[GeneratedFile]) -> Result<()> {
    for file in files {
        let full = root.join(&file.path);
        if let Some(parent) = full.parent() {
            std::fs::create_dir_all(parent).map_err(|source| Error::WriteOutput {
                path: parent.to_path_buf(),
                source,
            })?;
        }
        std::fs::write(&full, &file.contents).map_err(|source| Error::WriteOutput {
            path: full.clone(),
            source,
        })?;
    }
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::{raw_method, raw_type_str, url_arg, Imports};
    use crate::ir::{Endpoint, PathParam, Prim, TypeRef};

    fn endpoint(path: &str, params: Vec<PathParam>, response: Option<TypeRef>) -> Endpoint {
        Endpoint {
            module: "m".to_string(),
            method_name: "op".to_string(),
            http_method: "GET",
            path: path.to_string(),
            path_params: params,
            query_params: Vec::new(),
            request_body: None,
            response,
            docstring: None,
            emittable: true,
        }
    }

    #[test]
    fn raw_type_str_renders_every_shape() {
        let mut i = Imports::default();
        let str_of = |t: &TypeRef, i: &mut Imports| raw_type_str(t, i);
        assert_eq!(str_of(&TypeRef::Primitive(Prim::Str), &mut i), "str");
        assert_eq!(str_of(&TypeRef::Primitive(Prim::Int), &mut i), "int");
        assert_eq!(str_of(&TypeRef::Primitive(Prim::Float), &mut i), "float");
        assert_eq!(str_of(&TypeRef::Primitive(Prim::Bool), &mut i), "bool");
        assert_eq!(str_of(&TypeRef::Primitive(Prim::Any), &mut i), "typing.Any");
        assert_eq!(
            str_of(&TypeRef::Primitive(Prim::Datetime), &mut i),
            "dt.datetime"
        );
        assert_eq!(str_of(&TypeRef::Primitive(Prim::Date), &mut i), "dt.date");
        assert_eq!(
            str_of(&TypeRef::Named("FooBar".to_string()), &mut i),
            "FooBar"
        );
        assert_eq!(
            str_of(
                &TypeRef::Optional(Box::new(TypeRef::Primitive(Prim::Str))),
                &mut i
            ),
            "typing.Optional[str]"
        );
        assert_eq!(
            str_of(
                &TypeRef::List(Box::new(TypeRef::Primitive(Prim::Int))),
                &mut i
            ),
            "typing.List[int]"
        );
        assert_eq!(
            str_of(
                &TypeRef::Set(Box::new(TypeRef::Primitive(Prim::Str))),
                &mut i
            ),
            "typing.Set[str]"
        );
        assert_eq!(
            str_of(
                &TypeRef::Dict(
                    Box::new(TypeRef::Primitive(Prim::Str)),
                    Box::new(TypeRef::Named("Owner".to_string()))
                ),
                &mut i
            ),
            "typing.Dict[str, Owner]"
        );
        assert_eq!(
            str_of(
                &TypeRef::Union(vec![
                    TypeRef::Primitive(Prim::Str),
                    TypeRef::Primitive(Prim::Int)
                ]),
                &mut i
            ),
            "typing.Union[str, int]"
        );
        assert_eq!(
            str_of(
                &TypeRef::Literal(vec!["a".to_string(), "b".to_string()]),
                &mut i
            ),
            "typing.Literal[\"a\", \"b\"]"
        );
        let rendered = i.render();
        assert!(rendered.contains("from ..types.foo_bar import FooBar"));
        assert!(rendered.contains("import datetime as dt"));
    }

    #[test]
    fn url_arg_is_plain_or_interpolated() {
        let plain = endpoint(
            "/urls/MixedCase",
            vec![],
            Some(TypeRef::Primitive(Prim::Str)),
        );
        assert_eq!(url_arg(&plain), "\"urls/MixedCase\"");
        let interp = endpoint(
            "/things/{id}",
            vec![PathParam {
                wire_name: "id".to_string(),
                py_name: "id".to_string(),
                type_ref: TypeRef::Primitive(Prim::Str),
            }],
            Some(TypeRef::Primitive(Prim::Str)),
        );
        assert_eq!(url_arg(&interp), "f\"things/{jsonable_encoder(id)}\"");
    }

    #[test]
    fn raw_method_handles_a_none_response_and_a_summary() {
        let mut i = Imports::default();
        let mut ep = endpoint("/x", vec![], None);
        ep.docstring = Some("Does a thing.".to_string());
        let out = raw_method(&ep, false, &mut i);
        // Summary line, a `None` return with no parse, and no dangling parser import.
        assert!(out.contains("Does a thing."), "{out}");
        assert!(out.contains("-> HttpResponse[None]:"), "{out}");
        assert!(
            out.contains("return HttpResponse(response=_response, data=None)"),
            "{out}"
        );
        assert!(!out.contains("parse_obj_as"), "{out}");
    }
}
