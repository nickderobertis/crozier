//! Rendering the IR to Python source files with minijinja.
//!
//! Three layers: Rust computes the semantics (resolved type expressions, the
//! exact import block, per-field annotations and defaults); minijinja templates
//! (`templates/`) lay out the file shape — e.g. `class_body.py.j2` arranges a
//! pydantic model's docstring, fields, and config block, applying the Fern
//! blank-line rules `ruff` does not impose; and `ruff format` (see [`crate::pyfmt`])
//! handles the line wrapping. The template layout is covered directly by render
//! tests (see this module's `tests`), so it iterates without the full pipeline.

use std::collections::{BTreeMap, BTreeSet};
use std::path::PathBuf;

use minijinja::{context, Environment};
use serde::Serialize;

use crate::error::{Error, Result};
use crate::ir::{
    Auth, Endpoint, ErrorClass, Field, Ir, ObjectType, Prim, RequestBody, TypeDecl, TypeRef,
};
use crate::naming;
use crate::wrap::Doc;

/// The header comment crozier writes atop every generated file. It differs from
/// Fern's by design; the comment-stripping normalizer removes it before any
/// comparison, so the wording is free.
const HEADER: &str = "# This file was auto-generated by crozier from an OpenAPI document.";

/// A generated file: a repo-relative path and its contents.
#[derive(Debug, Clone)]
pub struct GeneratedFile {
    /// Path relative to the output root.
    pub path: PathBuf,
    /// File contents.
    pub contents: String,
}

/// Python standard-library top-level modules crozier may import. Used to split
/// imports into Fern's two ordered groups (stdlib first, everything else second).
const STDLIB_MODULES: &[&str] = &[
    "typing",
    "datetime",
    "uuid",
    "importlib",
    "enum",
    "os",
    "sys",
    "json",
    "base64",
    "collections",
    "decimal",
    "abc",
];

/// Collects imports and renders them in Fern's order: group 1 is stdlib
/// (`import`s then `from`s), group 2 is everything else (`import`s then `from`s),
/// separated by a blank line. Names within a `from` and the statements within a
/// group are sorted.
#[derive(Default)]
struct Imports {
    /// Plain `import module [as alias]`, keyed by module for sorting.
    plain: BTreeMap<String, Option<String>>,
    /// `from module import a, b`, module -> sorted names.
    from: BTreeMap<String, BTreeSet<String>>,
}

impl Imports {
    fn add_plain(&mut self, module: &str) {
        self.plain.entry(module.to_string()).or_insert(None);
    }

    fn add_plain_as(&mut self, module: &str, alias: &str) {
        self.plain
            .insert(module.to_string(), Some(alias.to_string()));
    }

    fn add_from(&mut self, module: &str, name: &str) {
        self.from
            .entry(module.to_string())
            .or_default()
            .insert(name.to_string());
    }

    /// Is a module (or a `from` path's leading segment) part of the stdlib group?
    /// Relative imports (leading `.`) are never stdlib.
    fn is_stdlib(module: &str) -> bool {
        if module.starts_with('.') {
            return false;
        }
        let top = module.split('.').next().unwrap_or(module);
        STDLIB_MODULES.contains(&top)
    }

    fn render(&self) -> String {
        let (mut g1, mut g2): (Vec<String>, Vec<String>) = (Vec::new(), Vec::new());

        // Plain imports first within each group (BTreeMap iterates sorted).
        for (module, alias) in &self.plain {
            let line = match alias {
                Some(a) => format!("import {module} as {a}"),
                None => format!("import {module}"),
            };
            if Self::is_stdlib(module) {
                g1.push(line);
            } else {
                g2.push(line);
            }
        }
        // Then `from` imports within each group.
        for (module, names) in &self.from {
            let joined = names.iter().cloned().collect::<Vec<_>>().join(", ");
            let line = format!("from {module} import {joined}");
            if Self::is_stdlib(module) {
                g1.push(line);
            } else {
                g2.push(line);
            }
        }

        let groups: Vec<String> = [g1, g2]
            .into_iter()
            .filter(|g| !g.is_empty())
            .map(|g| g.join("\n"))
            .collect();
        groups.join("\n\n")
    }
}

/// Render a resolved type to a [`Doc`] expression, registering needed imports.
/// The `Doc` renders flat; `ruff format` handles any line wrapping downstream.
fn render_type(t: &TypeRef, imports: &mut Imports) -> Doc {
    match t {
        TypeRef::Primitive(p) => match p {
            Prim::Str => Doc::atom("str"),
            Prim::Int | Prim::Long => Doc::atom("int"),
            Prim::Float => Doc::atom("float"),
            Prim::Bool => Doc::atom("bool"),
            Prim::Any => {
                imports.add_plain("typing");
                Doc::atom("typing.Any")
            }
            Prim::Datetime => {
                imports.add_plain_as("datetime", "dt");
                Doc::atom("dt.datetime")
            }
            Prim::Date => {
                imports.add_plain_as("datetime", "dt");
                Doc::atom("dt.date")
            }
        },
        TypeRef::Named(class) => {
            let module = naming::module_name(class);
            imports.add_from(&format!(".{module}"), class);
            Doc::atom(class.clone())
        }
        TypeRef::Optional(inner) => {
            imports.add_plain("typing");
            Doc::group("typing.Optional[", vec![render_type(inner, imports)], "]")
        }
        TypeRef::Literal(values) => {
            imports.add_plain("typing");
            let items = values
                .iter()
                .map(|v| Doc::atom(format!("\"{v}\"")))
                .collect();
            Doc::group("typing.Literal[", items, "]")
        }
        TypeRef::List(inner) => {
            imports.add_plain("typing");
            Doc::group("typing.List[", vec![render_type(inner, imports)], "]")
        }
        TypeRef::Set(inner) => {
            imports.add_plain("typing");
            Doc::group("typing.Set[", vec![render_type(inner, imports)], "]")
        }
        TypeRef::Dict(k, v) => {
            imports.add_plain("typing");
            Doc::group(
                "typing.Dict[",
                vec![render_type(k, imports), render_type(v, imports)],
                "]",
            )
        }
        TypeRef::Union(variants) => {
            imports.add_plain("typing");
            let items = variants.iter().map(|v| render_type(v, imports)).collect();
            Doc::group("typing.Union[", items, "]")
        }
    }
}

/// A field prepared for emission: its Python name, resolved annotation, the
/// `= <default>` suffix, and an optional docstring.
struct RenderedField {
    py_name: String,
    annotation: Doc,
    default: String,
    docstring: Option<String>,
}

/// The view model handed to `object.py.j2`. Rust computes the semantics (imports,
/// per-field annotation strings, defaults); the template lays out the class body
/// (field order, docstrings, the blank-line rules `ruff` does not impose) and
/// `ruff format` handles the line wrapping. This split keeps the byte-sensitive
/// logic in testable Rust while the file's *shape* lives in the template.
#[derive(Serialize)]
struct ObjectView {
    header: &'static str,
    imports: String,
    class_name: String,
    bases: String,
    /// The class docstring's raw text (the template applies the `docstring` filter).
    docstring: Option<String>,
    fields: Vec<FieldView>,
}

/// One field as the object template consumes it: the flat declaration line
/// (`name: annotation = default`, wrapped later by `ruff`) and the raw docstring.
#[derive(Serialize)]
struct FieldView {
    decl: String,
    docstring: Option<String>,
}

/// Compute a field's annotation + default, registering imports.
fn render_field(field: &Field, imports: &mut Imports) -> RenderedField {
    let inner = render_type(&field.type_ref, imports);
    let typ = if field.optional {
        imports.add_plain("typing");
        Doc::group("typing.Optional[", vec![inner], "]")
    } else {
        inner
    };

    // Fern carries the wire alias in `FieldMetadata` inside the `Annotated`, not
    // in `pydantic.Field`.
    let annotation = if field.needs_alias() {
        imports.add_plain("typing_extensions");
        imports.add_from("..core.serialization", "FieldMetadata");
        Doc::group(
            "typing_extensions.Annotated[",
            vec![
                typ,
                Doc::atom(format!("FieldMetadata(alias=\"{}\")", field.wire_name)),
            ],
            "]",
        )
    } else {
        typ
    };

    RenderedField {
        py_name: field.py_name.clone(),
        annotation,
        default: field_default(field, imports),
        docstring: field.docstring.clone(),
    }
}

/// The `= <default>` suffix for a field. A documented field carries a
/// `pydantic.Field` (with `default=None` when optional); an undocumented
/// optional field defaults to `None`; a required undocumented field has none.
fn field_default(field: &Field, imports: &mut Imports) -> String {
    if field.docstring.is_some() {
        imports.add_plain("pydantic");
        if field.optional {
            " = pydantic.Field(default=None)".to_string()
        } else {
            " = pydantic.Field()".to_string()
        }
    } else if field.optional {
        " = None".to_string()
    } else {
        String::new()
    }
}

/// Build the minijinja environment with the embedded templates.
fn environment() -> Environment<'static> {
    let mut env = Environment::new();
    env.set_keep_trailing_newline(true);
    env.set_trim_blocks(true);
    env.set_lstrip_blocks(true);
    // Render a raw docstring into an indented, backslash-escaped triple-quoted
    // block; templates apply it with `{{ text | docstring }}`.
    env.add_filter("docstring", |text: String| render_docstring(&text, 4));
    env.add_template("version.py", include_str!("../templates/version.py.j2"))
        .expect("version template compiles");
    env.add_template("object.py", include_str!("../templates/object.py.j2"))
        .expect("object template compiles");
    env.add_template(
        "class_body.py",
        include_str!("../templates/class_body.py.j2"),
    )
    .expect("class_body template compiles");
    env.add_template("alias.py", include_str!("../templates/alias.py.j2"))
        .expect("alias template compiles");
    env.add_template(
        "raw_client.py",
        include_str!("../templates/raw_client.py.j2"),
    )
    .expect("raw_client template compiles");
    env.add_template("error.py", include_str!("../templates/error.py.j2"))
        .expect("error template compiles");
    env.add_template("file.py", include_str!("../templates/file.py.j2"))
        .expect("file template compiles");
    env
}

/// Generate every file crozier currently emits for an IR.
pub fn generate(ir: &Ir) -> Result<Vec<GeneratedFile>> {
    let env = environment();
    let mut files = Vec::new();
    let pkg = &ir.package_name;

    // version.py
    let version = render(
        &env,
        "version.py",
        "version.py",
        context! { project_name => ir.project_name },
    )?;
    files.push(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/version.py")),
        contents: version,
    });

    // py.typed (an empty marker file).
    files.push(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/py.typed")),
        contents: String::new(),
    });

    // One file per generated type.
    for decl in &ir.types {
        let file = render_type_decl(&env, decl)?;
        files.push(GeneratedFile {
            path: PathBuf::from(format!("src/{pkg}/types/{}.py", decl.module())),
            contents: file,
        });
    }

    // Fern's static core runtime, emitted verbatim (see assets/README.md), plus
    // the auth-shaped `client_wrapper.py`.
    files.extend(core_files(pkg));
    files.push(client_wrapper_file(pkg, &ir.project_name, &ir.auth));

    // One package marker per endpoint client module. Fern's `__init__.py` here is
    // a comment-only header, so the comment-stripped form is four blank lines.
    for module in &ir.endpoint_modules {
        files.push(GeneratedFile {
            path: PathBuf::from(format!("src/{pkg}/{module}/__init__.py")),
            contents: "\n\n\n\n".to_string(),
        });
    }

    // Per-tag `raw_client.py` and `client.py`, but only for modules whose every
    // operation is within the subset crozier emits today (see
    // `Endpoint::emittable`); the rest await wider endpoint support. The modules
    // that clear this bar (in sorted order) also drive the root client below.
    let mut emittable_modules: Vec<&String> = Vec::new();
    for module in &ir.endpoint_modules {
        let eps: Vec<&Endpoint> = ir
            .endpoints
            .iter()
            .filter(|e| &e.module == module)
            .collect();
        if !eps.is_empty() && eps.iter().all(|e| e.emittable) {
            files.push(raw_client_file(&env, pkg, module, &eps)?);
            let cx = ClientCtx {
                pkg,
                client_name: &ir.client_name,
                module,
                types: &ir.types,
                auth: &ir.auth,
            };
            files.push(client_file(&env, &cx, &eps)?);
            emittable_modules.push(module);
        }
    }
    emittable_modules.sort();

    // Root client: `FernApi`/`AsyncFernApi` aggregating the tag clients. Emitted
    // only when there is at least one tag client to aggregate.
    if !emittable_modules.is_empty() {
        files.push(root_client_file(
            &env,
            pkg,
            &ir.client_name,
            &emittable_modules,
            &ir.auth,
        )?);
    }

    // The `errors/` package: one exception class per distinct declared error,
    // plus the lazy-loading `__init__.py` aggregator. Emitted only when there are
    // errors to declare.
    files.extend(error_files(&env, pkg, &ir.errors)?);

    // Package aggregators: `types/__init__.py` over the type layer, and the
    // package-root `__init__.py` re-exporting types, errors, the endpoint
    // submodules, the root client, and `__version__`.
    if !ir.types.is_empty() {
        files.push(types_init_file(pkg, &ir.types));
    }
    if !emittable_modules.is_empty() {
        files.push(root_init_file(
            pkg,
            &ir.client_name,
            &ir.types,
            &ir.errors,
            &emittable_modules,
        ));
    }

    // Generated `README.md` (usage examples from the first endpoint) and the
    // per-endpoint `reference.md`.
    if let Some(readme) = readme_file(ir) {
        files.push(readme);
    }
    if let Some(reference) = reference_file(ir, &emittable_modules) {
        files.push(reference);
    }

    // Project-root scaffolding (pyproject.toml, requirements.txt, metadata).
    files.extend(scaffolding_files(pkg, &ir.project_name));

    // Final wrapping is delegated to `ruff format` (the tool Fern runs), so the
    // emitters above produce content-correct Python without reproducing ruff's
    // line layout. `.py` files are formatted in place; non-Python outputs
    // (Markdown, TOML, JSON, the `py.typed` marker) are left untouched.
    format_python_files(pkg, &mut files)?;

    Ok(files)
}

/// Generate the `errors/` package: an exception module per class and the
/// lazy-loading `__init__.py` re-exporting them. Empty when there are no errors.
fn error_files(
    env: &Environment<'static>,
    pkg: &str,
    errors: &[ErrorClass],
) -> Result<Vec<GeneratedFile>> {
    if errors.is_empty() {
        return Ok(Vec::new());
    }
    let mut files = Vec::new();
    for err in errors {
        let mut imports = Imports::default();
        imports.add_plain("typing");
        imports.add_from("..core.api_error", "ApiError");
        // Registers the body type's `..types.<module>` import.
        let body_type = raw_type_str(&err.body_type, &mut imports);
        let module = naming::module_name(&err.class_name);
        let contents = render(
            env,
            "error.py",
            &err.class_name,
            context! {
                header => HEADER,
                imports => imports.render(),
                class_name => err.class_name,
                body_type => body_type,
                status_code => err.status_code,
            },
        )?;
        files.push(GeneratedFile {
            path: PathBuf::from(format!("src/{pkg}/errors/{module}.py")),
            contents,
        });
    }
    files.push(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/errors/__init__.py")),
        contents: lazy_loader_init(errors),
    });
    Ok(files)
}

/// Fern's package-`__init__` lazy loader for the `errors/` package: a
/// `TYPE_CHECKING` import block, a `_dynamic_imports` map, the static
/// `__getattr__`/`__dir__` machinery, and `__all__`. The error classes arrive
/// pre-sorted, so all three sections are alphabetical.
fn lazy_loader_init(errors: &[ErrorClass]) -> String {
    let type_checking: String = errors
        .iter()
        .map(|e| {
            format!(
                "    from .{} import {}\n",
                naming::module_name(&e.class_name),
                e.class_name
            )
        })
        .collect();
    let pairs: Vec<(String, String)> = errors
        .iter()
        .map(|e| {
            (
                e.class_name.clone(),
                format!(".{}", naming::module_name(&e.class_name)),
            )
        })
        .collect();
    let names: Vec<String> = errors.iter().map(|e| e.class_name.clone()).collect();
    render_lazy_loader(&type_checking, &pairs, &names)
}

/// Assemble a lazy-loader `__init__.py` from its `TYPE_CHECKING` block, the
/// `_dynamic_imports` pairs `(exported name, ".module")`, and the `__all__`
/// names. The `_dynamic_imports` map and `__all__` are always alphabetical
/// (sorted here); only the `TYPE_CHECKING` block's order is caller-controlled.
/// The header collapses to the four leading blank lines Fern's comment header
/// strips to.
fn render_lazy_loader(type_checking: &str, pairs: &[(String, String)], names: &[String]) -> String {
    let mut pairs = pairs.to_vec();
    pairs.sort();
    let mut names = names.to_vec();
    names.sort();

    // `_dynamic_imports` and `__all__` are emitted flat; `ruff format` wraps them
    // (one entry per line with a trailing comma) when they overflow.
    let entries: Vec<String> = pairs
        .iter()
        .map(|(n, m)| format!("\"{n}\": \"{m}\""))
        .collect();
    let dynamic = format!(
        "_dynamic_imports: typing.Dict[str, str] = {{{}}}",
        entries.join(", ")
    );
    let all_entries: Vec<String> = names.iter().map(|n| format!("\"{n}\"")).collect();
    let all = format!("__all__ = [{}]", all_entries.join(", "));

    format!(
        "{HEADER}\n\n\n\nimport typing\nfrom importlib import import_module\n\nif typing.TYPE_CHECKING:\n{type_checking}{dynamic}\n\n\ndef __getattr__(attr_name: str) -> typing.Any:\n    module_name = _dynamic_imports.get(attr_name)\n    if module_name is None:\n        raise AttributeError(f\"No {{attr_name}} found in _dynamic_imports for module name -> {{__name__}}\")\n    try:\n        module = import_module(module_name, __package__)\n        if module_name == f\".{{attr_name}}\":\n            return module\n        else:\n            return getattr(module, attr_name)\n    except ImportError as e:\n        raise ImportError(f\"Failed to import {{attr_name}} from {{module_name}}: {{e}}\") from e\n    except AttributeError as e:\n        raise AttributeError(f\"Failed to get {{attr_name}} from {{module_name}}: {{e}}\") from e\n\n\ndef __dir__():\n    lazy_attrs = list(_dynamic_imports.keys())\n    return sorted(lazy_attrs)\n\n\n{all}\n"
    )
}

/// The `types/__init__.py` lazy loader. `_dynamic_imports`/`__all__` are
/// alphabetical; the `TYPE_CHECKING` block follows Fern's endpoint-traversal
/// order, which for this corpus is the `Types*` types in reverse declaration
/// order followed by the remaining types alphabetically.
fn types_init_file(pkg: &str, types: &[TypeDecl]) -> GeneratedFile {
    // Each declaration may export more than its primary name (a discriminated
    // union also exports its per-variant wrappers), all sharing the decl module.
    let mut module_of: std::collections::HashMap<String, String> = std::collections::HashMap::new();
    let mut names: Vec<String> = Vec::new();
    for decl in types {
        for n in decl.exported_names() {
            names.push(n.to_string());
            module_of.insert(n.to_string(), decl.module().to_string());
        }
    }

    // TYPE_CHECKING imports are emitted alphabetically. Their order is never
    // executed and the e2e canonicalizes it with isort, so crozier sorts
    // straightforwardly rather than reproducing Fern's traversal order.
    let mut ordered = names.clone();
    ordered.sort();

    // Consecutive names sharing a module collapse into one `from .mod import a, b`
    // line (Fern groups a discriminated union's names on a single import).
    let module = |n: &str| {
        module_of
            .get(n)
            .cloned()
            .unwrap_or_else(|| naming::module_name(n))
    };
    let mut type_checking = String::new();
    let mut i = 0;
    while i < ordered.len() {
        let m = module(&ordered[i]);
        let mut group = vec![ordered[i].clone()];
        while i + 1 < ordered.len() && module(&ordered[i + 1]) == m {
            i += 1;
            group.push(ordered[i].clone());
        }
        type_checking.push_str(&format!("    from .{m} import {}\n", group.join(", ")));
        i += 1;
    }

    let pairs: Vec<(String, String)> = names
        .iter()
        .map(|n| (n.clone(), format!(".{}", module(n))))
        .collect();
    GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/types/__init__.py")),
        contents: render_lazy_loader(&type_checking, &pairs, &names),
    }
}

/// The package-root `__init__.py` lazy loader, re-exporting the type layer, the
/// errors, the endpoint submodules, the root client, and `__version__`. Every
/// section is alphabetical; the `TYPE_CHECKING` block groups the imports by
/// source (`.types`, `.errors`, submodules, `.client`, `.version`).
fn root_init_file(
    pkg: &str,
    client_name: &str,
    types: &[TypeDecl],
    errors: &[ErrorClass],
    modules: &[&String],
) -> GeneratedFile {
    let async_name = format!("Async{client_name}");

    let mut type_names: Vec<String> = types
        .iter()
        .flat_map(|t| t.exported_names())
        .map(str::to_string)
        .collect();
    type_names.sort();
    let mut error_names: Vec<String> = errors.iter().map(|e| e.class_name.clone()).collect();
    error_names.sort();
    let mut module_names: Vec<String> = modules.iter().map(|m| (*m).clone()).collect();
    module_names.sort();

    // Grouped TYPE_CHECKING block (each group alphabetical, ruff-wrapped at 120).
    let mut tc = String::new();
    tc.push_str(&from_import_block(".types", &type_names, 4));
    tc.push_str(&from_import_block(".errors", &error_names, 4));
    tc.push_str(&from_import_block(".", &module_names, 4));
    tc.push_str(&from_import_block(
        ".client",
        &[async_name.clone(), client_name.to_string()],
        4,
    ));
    tc.push_str(&from_import_block(
        ".version",
        &["__version__".to_string()],
        4,
    ));

    // `_dynamic_imports` pairs: every symbol → its source module.
    let mut pairs: Vec<(String, String)> = Vec::new();
    for n in &type_names {
        pairs.push((n.clone(), ".types".to_string()));
    }
    for n in &error_names {
        pairs.push((n.clone(), ".errors".to_string()));
    }
    for m in &module_names {
        pairs.push((m.clone(), format!(".{m}")));
    }
    pairs.push((client_name.to_string(), ".client".to_string()));
    pairs.push((async_name, ".client".to_string()));
    pairs.push(("__version__".to_string(), ".version".to_string()));

    let names: Vec<String> = pairs.iter().map(|(n, _)| n.clone()).collect();
    GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/__init__.py")),
        contents: render_lazy_loader(&tc, &pairs, &names),
    }
}

/// Whether Fern shows a `...` placeholder for an endpoint's body in the README's
/// abbreviated snippets (versus empty parens). Empirically `...` appears for a
/// "fully-specified" body: an inline object whose every field is required, or a
/// container body. A body with any optional field, a union, an enum/scalar, or no
/// body renders empty parens.
fn complex_body(ep: &Endpoint, types: &[TypeDecl]) -> bool {
    match &ep.request_body {
        None => false,
        Some(RequestBody::Bytes) => true,
        Some(RequestBody::Inline(fields)) => fields.iter().all(|f| f.spec_required),
        Some(RequestBody::Form(form)) => {
            form.fields.iter().any(|f| f.is_file) || form.fields.iter().all(|f| f.spec_required)
        }
        Some(RequestBody::Single(s)) => is_complex_type(&s.type_ref, types),
    }
}

/// Whether a single-argument body type renders as a container (list/set/map) or a
/// plain object — not a union, enum, or scalar.
fn is_complex_type(t: &TypeRef, types: &[TypeDecl]) -> bool {
    match t {
        TypeRef::List(_) | TypeRef::Set(_) | TypeRef::Dict(..) => true,
        TypeRef::Optional(inner) => is_complex_type(inner, types),
        TypeRef::Named(n) => types
            .iter()
            .find(|d| d.name() == n)
            .is_some_and(|d| match d {
                TypeDecl::Object(_) => true,
                // A union (named or discriminated) renders empty parens.
                TypeDecl::DiscriminatedUnion(_) => false,
                TypeDecl::Alias(a) => {
                    !matches!(a.target, TypeRef::Union(_)) && is_complex_type(&a.target, types)
                }
            }),
        TypeRef::Union(_) | TypeRef::Primitive(_) | TypeRef::Literal(_) => false,
    }
}

/// Render an abbreviated call `<prefix>(...)` for the README snippets: empty parens
/// when the body is not complex, else a `...` placeholder, ruff-wrapped at width 88
/// (the snippet line length) onto its own line when the flat form overflows.
fn abbrev_call(indent: usize, prefix: &str, complex: bool) -> String {
    let pad = " ".repeat(indent);
    if !complex {
        return format!("{pad}{prefix}()");
    }
    let flat = format!("{pad}{prefix}(...)");
    if flat.len() <= 88 {
        flat
    } else {
        format!("{pad}{prefix}(\n{}...\n{pad})", " ".repeat(indent + 4))
    }
}

/// The generated `README.md`: mostly static prose with the SDK name/package
/// substituted and a worked usage example (sync + async) synthesized from the
/// first endpoint. Compared verbatim (README is not comment-stripped). Emitted
/// only when there is an endpoint to demonstrate.
fn readme_file(ir: &Ir) -> Option<GeneratedFile> {
    // Fern demonstrates the first emittable endpoint that has a request body (the
    // most illustrative call), falling back to the first emittable endpoint.
    let first = ir
        .endpoints
        .iter()
        .find(|e| e.emittable && e.request_body.is_some())
        .or_else(|| ir.endpoints.iter().find(|e| e.emittable))?;
    let pkg = &ir.package_name;
    let org = naming::to_pascal_case(pkg);
    let async_name = format!("Async{}", ir.client_name);

    // The abbreviated calls in the error-handling and advanced sections show `...`
    // for an endpoint with a complex (object/container/union) body, else empty
    // parens; the error/raw-response calls are ruff-wrapped at the snippet width 88.
    let complex = complex_body(first, &ir.types);
    let err_call = abbrev_call(
        4,
        &format!("client.{}.{}", first.module, first.method_name),
        complex,
    );
    let raw_call = abbrev_call(
        0,
        &format!(
            "response = client.{}.with_raw_response.{}",
            first.module, first.method_name
        ),
        complex,
    );
    let retry_call = format!(
        "client.{}.{}({}request_options={{",
        first.module,
        first.method_name,
        if complex { "..., " } else { "" }
    );

    let sync_example = {
        let mut ctx = ExampleCtx {
            types: &ir.types,
            referenced: BTreeSet::new(),
            uses_datetime: false,
            auth: &ir.auth,
        };
        build_example(first, false, &first.module, pkg, &ir.client_name, &mut ctx)?.join("\n")
    };
    let async_example = {
        let mut ctx = ExampleCtx {
            types: &ir.types,
            referenced: BTreeSet::new(),
            uses_datetime: false,
            auth: &ir.auth,
        };
        build_example(first, true, &first.module, pkg, &ir.client_name, &mut ctx)?.join("\n")
    };

    let contents = include_str!("../assets/scaffolding/README.md.tmpl")
        .replace("@@ORG@@", &org)
        .replace("@@PROJECT@@", &ir.project_name)
        .replace("@@PKG@@", pkg)
        .replace("@@CLIENT@@", &ir.client_name)
        .replace("@@ASYNC@@", &async_name)
        .replace("@@ERR_CALL@@", &err_call)
        .replace("@@RAW_CALL@@", &raw_call)
        .replace("@@RETRY_CALL@@", &retry_call)
        .replace("@@USAGE@@", &sync_example)
        .replace("@@ASYNC_EXAMPLE@@", &async_example);
    Some(GeneratedFile {
        path: PathBuf::from("README.md"),
        contents,
    })
}

/// The generated `reference.md`: a per-endpoint reference grouped by tag, each
/// entry a collapsible `<details>` with an optional description, a worked sync
/// usage example, and a parameter table. Endpoints without an example (a raw
/// bytes body) are omitted, exactly as Fern does. Compared verbatim.
fn reference_file(ir: &Ir, modules: &[&String]) -> Option<GeneratedFile> {
    let pkg = &ir.package_name;
    let mut lines: Vec<String> = vec!["# Reference".to_string()];
    let mut any = false;

    for module in modules {
        let eps: Vec<&Endpoint> = ir
            .endpoints
            .iter()
            .filter(|e| &e.module == *module && e.emittable)
            .filter(|e| !matches!(e.request_body, Some(RequestBody::Bytes)))
            .collect();
        if eps.is_empty() {
            continue;
        }
        any = true;
        lines.push(format!("## {}", naming::to_pascal_case(module)));
        for ep in eps {
            lines.extend(reference_entry(ir, ep, module, pkg));
            lines.push(String::new());
        }
    }
    if !any {
        return None;
    }
    // The last entry's trailing blank line is kept: Fern ends the file with one.
    Some(GeneratedFile {
        path: PathBuf::from("reference.md"),
        contents: format!("{}\n", lines.join("\n")),
    })
}

/// One `reference.md` endpoint entry (a `<details>` block).
fn reference_entry(ir: &Ir, ep: &Endpoint, module: &str, pkg: &str) -> Vec<String> {
    let mut imports = Imports::default();
    let mp = method_params(ep, &mut imports);

    // The example (sync form). Bytes bodies are filtered out before this point.
    let mut ctx = ExampleCtx {
        types: &ir.types,
        referenced: BTreeSet::new(),
        uses_datetime: false,
        auth: &ir.auth,
    };
    let example =
        build_example(ep, false, module, pkg, &ir.client_name, &mut ctx).unwrap_or_default();

    // The parameter rows, in signature order, then `request_options`.
    let mut params: Vec<(String, String, String)> = Vec::new();
    for (name, ty) in &mp.path {
        params.push((name.clone(), ty.clone(), " ".to_string()));
    }
    for dp in mp.query.iter().chain(&mp.header).chain(&mp.body) {
        let suffix = match &dp.description {
            Some(d) => format!(" — {d}"),
            None => " ".to_string(),
        };
        // Fern renders a `core.File` type in the reference parameter table with a
        // leading `from __future__ import annotations` (an artifact of how it prints
        // the forward-referenced file type); reproduce it so the table matches.
        let annotation = if dp.annotation == "core.File" {
            "from __future__ import annotations\n\ncore.File".to_string()
        } else {
            dp.annotation.clone()
        };
        params.push((dp.name.clone(), annotation, suffix));
    }
    params.push((
        "request_options".to_string(),
        "typing.Optional[RequestOptions]".to_string(),
        " — Request-specific configuration.".to_string(),
    ));

    let has_args =
        !mp.path.is_empty() || !mp.query.is_empty() || !mp.header.is_empty() || !mp.body.is_empty();
    let dots = if has_args { "..." } else { "" };

    let mut out: Vec<String> = Vec::new();
    out.push(format!(
        "<details><summary><code>client.{module}.<a href=\"src/{pkg}/{module}/client.py\">{}</a>({dots})</code></summary>",
        ep.method_name
    ));
    out.push("<dl>".to_string());
    out.push("<dd>".to_string());
    out.push(String::new());

    // Optional description block.
    if let Some(desc) = &ep.docstring {
        out.push("#### 📝 Description".to_string());
        out.push(String::new());
        out.extend(["<dl>", "<dd>", "", "<dl>", "<dd>", ""].map(String::from));
        out.extend(desc.split('\n').map(String::from));
        out.extend(["</dd>", "</dl>", "</dd>", "</dl>", ""].map(String::from));
    }

    // Usage block.
    out.push("#### 🔌 Usage".to_string());
    out.push(String::new());
    out.extend(["<dl>", "<dd>", "", "<dl>", "<dd>", ""].map(String::from));
    out.push("```python".to_string());
    out.extend(example);
    out.push(String::new());
    out.push("```".to_string());
    out.extend(["</dd>", "</dl>", "</dd>", "</dl>", ""].map(String::from));

    // Parameters block.
    out.push("#### ⚙️ Parameters".to_string());
    out.push(String::new());
    out.extend(["<dl>", "<dd>", ""].map(String::from));
    for (i, (name, annot, suffix)) in params.iter().enumerate() {
        out.extend(["<dl>", "<dd>", ""].map(String::from));
        out.push(format!("**{name}:** `{annot}`{suffix}"));
        out.push("    ".to_string());
        out.push("</dd>".to_string());
        out.push("</dl>".to_string());
        if i + 1 < params.len() {
            out.push(String::new());
        }
    }
    out.extend(["</dd>", "</dl>", "", "", "</dd>", "</dl>", "</details>"].map(String::from));
    out
}

/// Render `from <module> import <names>` at `indent` spaces, sorted, on one line.
/// `ruff format` wraps it into a parenthesized per-name block if it overflows.
/// Returns the statement with its trailing newline.
fn from_import_block(module: &str, names: &[String], indent: usize) -> String {
    // An empty group emits nothing — a `from <module> import` with no names is a
    // syntax error, and the source module (e.g. `errors/`) is not emitted when
    // there is nothing to import from it. The corpus always has errors, so this
    // path is only reached for specs that declare none.
    if names.is_empty() {
        return String::new();
    }
    let pad = " ".repeat(indent);
    let mut names = names.to_vec();
    names.sort();
    format!("{pad}from {module} import {}\n", names.join(", "))
}

