/// Shared context for emitting one per-tag `client.py`: the import package name,
/// the root client name, the module, and the type table the example generator
/// consults. Bundled so the client helpers stay within clippy's argument limit.
struct ClientCtx<'a> {
    pkg: &'a str,
    client_name: &'a str,
    module: &'a str,
    types: &'a [TypeDecl],
    /// Hoisted tag-scoped types, for the example generator's constructors.
    tag_decls: &'a [TagTypeDecl],
    auth: &'a Auth,
    has_environment: bool,
    tag_types: &'a BTreeMap<String, String>,
    global_headers: &'a [GlobalHeader],
}

/// Assemble a per-tag `client.py`: the sync and async high-level clients that
/// wrap the raw client, return `_response.data`, and carry a worked `Examples`
/// docstring.
fn client_file(
    env: &Environment<'static>,
    cx: &ClientCtx,
    endpoints: &[&Endpoint],
) -> Result<GeneratedFile> {
    let stem = naming::to_pascal_case(cx.module);
    let mut imports = Imports::at(RefLoc::Client(cx.module.to_string()), cx.tag_types);
    imports.add_plain("typing");
    imports.add_from("..core.client_wrapper", "AsyncClientWrapper");
    imports.add_from("..core.client_wrapper", "SyncClientWrapper");
    imports.add_from("..core.request_options", "RequestOptions");
    imports.add_from(".raw_client", &format!("Raw{stem}Client"));
    imports.add_from(".raw_client", &format!("AsyncRaw{stem}Client"));

    let sync = client_class(env, cx, endpoints, false, &mut imports)?;
    let async_class = client_class(env, cx, endpoints, true, &mut imports)?;
    // The `OMIT` sentinel is declared above the classes whenever a request body
    // is present, exactly as in the raw client.
    let omit = if endpoints.iter().any(|e| e.request_body.is_some()) {
        "OMIT = typing.cast(typing.Any, ...)\n\n\n"
    } else {
        ""
    };
    let body = format!("{omit}{sync}\n\n\n{async_class}");
    let contents = render(
        env,
        "raw_client.py",
        cx.module,
        context! { header => HEADER, imports => imports.render(), body => body },
    )?;
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{}/{}/client.py", cx.pkg, cx.module)),
        contents,
    })
}

/// One high-level client class: `__init__`, the `with_raw_response` property, and
/// one wrapper method per operation. The class/wrapper/raw-client names are
/// derived from the module and `is_async`.
fn client_class(
    env: &Environment<'static>,
    cx: &ClientCtx,
    endpoints: &[&Endpoint],
    is_async: bool,
    imports: &mut Imports,
) -> Result<String> {
    let stem = naming::to_pascal_case(cx.module);
    let (class_name, wrapper, raw_client_cls) = if is_async {
        (
            format!("Async{stem}Client"),
            "AsyncClientWrapper",
            format!("AsyncRaw{stem}Client"),
        )
    } else {
        (
            format!("{stem}Client"),
            "SyncClientWrapper",
            format!("Raw{stem}Client"),
        )
    };
    // Each method is computed in Rust (signature, docstring, delegation body); the
    // template lays out the class skeleton and separates the methods.
    let methods: Vec<String> = endpoints
        .iter()
        .map(|ep| client_method(cx, ep, is_async, imports))
        .collect();
    let view = context! {
        class_name => class_name,
        wrapper => wrapper,
        raw_client_cls => raw_client_cls,
        methods => methods,
    };
    let rendered = render(env, "client_class.py", &class_name, view)?;
    Ok(rendered.trim_end_matches('\n').to_string())
}

/// One high-level wrapper method: the signature, the docstring (with a worked
/// example), and a body delegating to the raw client and returning
/// `_response.data`.
fn client_method(cx: &ClientCtx, ep: &Endpoint, is_async: bool, imports: &mut Imports) -> String {
    if ep.streaming {
        return client_stream_method(cx, ep, is_async, imports);
    }
    if ep.binary_response {
        return client_binary_stream_method(cx, ep, is_async, imports);
    }
    let mp = method_params(ep, imports);
    let return_type = if ep.http_method == "HEAD" {
        "typing.Dict[str, str]".to_string()
    } else {
        mp.inner.clone()
    };
    let sig = signature(ep, &mp, &return_type, is_async);
    let docstring = client_docstring(cx, ep, &mp, is_async);

    // Delegation: path params positionally, the rest as keywords, then
    // `request_options`, laid out with ruff's right-hand-split (line length 120).
    let await_ = if is_async { "await " } else { "" };
    let mut call_args: Vec<Doc> = Vec::new();
    for (name, _, _) in &mp.path {
        call_args.push(Doc::atom(name.clone()));
    }
    for dp in ordered_keyword_params(&mp.query, &mp.header, &mp.body) {
        call_args.push(Doc::atom(format!("{}={}", dp.name, dp.name)));
    }
    call_args.push(Doc::atom("request_options=request_options"));
    let open = format!("{await_}self._raw_client.{}(", ep.method_name);
    let call = format!(
        "        _response = {}",
        Doc::group(open, call_args, ")").flat()
    );
    let result = if ep.http_method == "HEAD" {
        "_response.headers"
    } else {
        "_response.data"
    };
    format!("{sig}\n{docstring}\n{call}\n        return {result}")
}

/// The high-level streaming method (sync or async): a plain generator that opens
/// the context-managed raw stream and yields each decoded chunk. Matches Fern's
/// `Iterator`/`AsyncIterator` wrapper over the raw client's SSE stream (issue #43).
fn client_stream_method(
    cx: &ClientCtx,
    ep: &Endpoint,
    is_async: bool,
    imports: &mut Imports,
) -> String {
    let mp = method_params(ep, imports);
    let iter_t = if is_async {
        "typing.AsyncIterator"
    } else {
        "typing.Iterator"
    };
    let return_type = format!("{iter_t}[{SSE_CHUNK}]");
    let sig = signature(ep, &mp, &return_type, is_async);
    let docstring = client_stream_docstring(cx, ep, &mp, is_async);

    // Delegation call arguments, in signature order (path positionally, the rest as
    // keywords, then `request_options`) — identical to the buffered high-level method.
    let mut call_args: Vec<Doc> = Vec::new();
    for (name, _, _) in &mp.path {
        call_args.push(Doc::atom(name.clone()));
    }
    for dp in ordered_keyword_params(&mp.query, &mp.header, &mp.body) {
        call_args.push(Doc::atom(format!("{}={}", dp.name, dp.name)));
    }
    call_args.push(Doc::atom("request_options=request_options"));
    let open = format!("self._raw_client.{}(", ep.method_name);
    let raw_call = Doc::group(open, call_args, ")").flat();
    let body = if is_async {
        format!(
            "        async with {raw_call} as r:\n            async for _chunk in r.data:\n                yield _chunk"
        )
    } else {
        format!("        with {raw_call} as r:\n            yield from r.data")
    };
    format!("{sig}\n{docstring}\n{body}")
}

/// The streaming high-level method docstring (indent 8): `Parameters`, a `Yields`
/// section (matching Fern's streaming methods), then a worked `Examples` block that
