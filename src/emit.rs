/// Build the worked `Examples` block for a method as logical (indent-0) lines,
/// or `None` when the request is not exampleable (a raw bytes body).
fn build_example(
    ep: &Endpoint,
    is_async: bool,
    module: &str,
    pkg: &str,
    client_name: &str,
    ctx: &mut ExampleCtx,
) -> Option<Vec<String>> {
    if matches!(ep.request_body, Some(RequestBody::Bytes)) {
        return None;
    }

    // The example call's keyword arguments, in signature order: path params,
    // required query/header params, then the request body (a single `request`, or
    // each required inlined field).
    let mut args: Vec<(Option<String>, Example)> = Vec::new();
    for pp in &ep.path_params {
        let v = ctx.value(&pp.type_ref, Slot::Named(&pp.wire_name));
        args.push((Some(pp.py_name.clone()), v));
    }
    for qp in ep.query_params.iter().filter(|q| q.required) {
        let v = if let TypeRef::List(inner) = &qp.type_ref {
            Example::List(vec![ctx.value(inner, Slot::Named(&qp.wire_name))])
        } else {
            ctx.value(&qp.type_ref, Slot::Named(&qp.wire_name))
        };
        args.push((Some(qp.py_name.clone()), v));
    }
    for hp in ep.header_params.iter().filter(|h| h.required) {
        let v = ctx.value(&hp.type_ref, Slot::Named(&hp.wire_name));
        args.push((Some(hp.py_name.clone()), v));
    }
    match &ep.request_body {
        Some(RequestBody::Single(s)) => {
            let v = ctx.value(&s.type_ref, Slot::Plain);
            args.push((Some("request".to_string()), v));
        }
        Some(RequestBody::Inline(fields)) => {
            for f in fields.iter().filter(|f| f.spec_required) {
                let v = ctx.value(&f.type_ref, Slot::Named(&f.wire_name));
                args.push((Some(f.py_name.clone()), v));
            }
        }
        // A form body: required non-file fields only (a file cannot be shown as a
        // literal, so Fern omits it from the example).
        Some(RequestBody::Form(form)) => {
            for f in form.fields.iter().filter(|f| f.spec_required && !f.is_file) {
                let v = ctx.value(&f.type_ref, Slot::Named(&f.wire_name));
                args.push((Some(f.py_name.clone()), v));
            }
        }
        _ => {}
    }

    let example_name = if is_async {
        format!("Async{client_name}")
    } else {
        client_name.to_string()
    };
    // The call, rendered at logical indent 0 (sync) or 4 (inside `main`, async).
    let call_indent = if is_async { 4 } else { 0 };
    let receiver = format!(
        "{}client.{module}.{}",
        if is_async { "await " } else { "" },
        ep.method_name
    );
    // `render` positions continuation lines but leaves the first line for the
    // caller to place, so prepend the base indent to it.
    let call = {
        let rendered = Example::Call(receiver, args).render(call_indent);
        format!("{}{rendered}", " ".repeat(call_indent))
    };

    // Preamble stdlib imports: `asyncio` for the async form, `datetime` when a
    // value needs it.
    let mut preamble: Vec<String> = Vec::new();
    if is_async {
        preamble.push("import asyncio".to_string());
    }
    if ctx.uses_datetime {
        preamble.push("import datetime".to_string());
    }

    // `from <pkg> import <client + referenced types>`, alphabetical, wrapped at
    // the snippet's default line length (88). Fern's examples are laid out by hand
    // (not `ruff format`) — see `Example::render` for why.
    let mut names: Vec<String> = ctx.referenced.iter().cloned().collect();
    names.push(example_name.clone());
    names.sort();
    let import_flat = format!("from {pkg} import {}", names.join(", "));
    let import_lines: Vec<String> = if import_flat.len() <= 88 {
        vec![import_flat]
    } else {
        let mut v = vec![format!("from {pkg} import (")];
        for n in &names {
            v.push(format!("    {n},"));
        }
        v.push(")".to_string());
        v
    };

    let client_block = vec![
        format!("client = {example_name}("),
        format!("    {},", auth_example(ctx.auth)),
        "    base_url=\"https://yourhost.com/path/to/api\",".to_string(),
        ")".to_string(),
    ];

    let mut out: Vec<String> = Vec::new();
    if !preamble.is_empty() {
        out.extend(preamble);
        out.push(String::new());
    }
    out.extend(import_lines);
    out.push(String::new());
    out.extend(client_block);
    if is_async {
        out.push(String::new());
        out.push(String::new());
        out.push("async def main() -> None:".to_string());
        out.extend(call.split('\n').map(String::from));
        out.push(String::new());
        out.push(String::new());
        out.push("asyncio.run(main())".to_string());
    } else {
        out.extend(call.split('\n').map(String::from));
    }
    Some(out)
}

/// Assemble the `raw_client.py` file for one client module: the sync and async
/// raw client classes, each wrapping every operation.
fn raw_client_file(
    env: &Environment<'static>,
    pkg: &str,
    module: &str,
    endpoints: &[&Endpoint],
) -> Result<GeneratedFile> {
    let mut imports = Imports::default();
    // Imports every raw client needs regardless of operation shape.
    imports.add_plain("typing");
    imports.add_from("json.decoder", "JSONDecodeError");
    imports.add_from("..core.api_error", "ApiError");
    imports.add_from("..core.client_wrapper", "AsyncClientWrapper");
    imports.add_from("..core.client_wrapper", "SyncClientWrapper");
    imports.add_from("..core.http_response", "AsyncHttpResponse");
    imports.add_from("..core.http_response", "HttpResponse");
    imports.add_from("..core.request_options", "RequestOptions");

    let class_stem = naming::to_pascal_case(module);
    let sync = raw_client_class(
        &format!("Raw{class_stem}Client"),
        "SyncClientWrapper",
        endpoints,
        false,
        &mut imports,
    );
    let async_class = raw_client_class(
        &format!("AsyncRaw{class_stem}Client"),
        "AsyncClientWrapper",
        endpoints,
        true,
        &mut imports,
    );
    // A module with any request body declares the `OMIT` sentinel above the
    // classes; `omit=OMIT` drops unset optional fields from the serialized body.
    let omit = if endpoints.iter().any(|e| e.request_body.is_some()) {
        "OMIT = typing.cast(typing.Any, ...)\n\n\n"
    } else {
        ""
    };
    let body = format!("{omit}{sync}\n\n\n{async_class}");

    let contents = render(
        env,
        "raw_client.py",
        module,
        context! {
            header => HEADER,
            imports => imports.render(),
            body => body,
        },
    )?;
    Ok(GeneratedFile {
        path: PathBuf::from(format!("src/{pkg}/{module}/raw_client.py")),
        contents,
    })
}

/// Render one raw-client class: the `__init__` binding the client wrapper, then
/// one method per operation separated by a blank line.
fn raw_client_class(
    class_name: &str,
    wrapper_type: &str,
    endpoints: &[&Endpoint],
    is_async: bool,
    imports: &mut Imports,
) -> String {
    let mut out = format!(
        "class {class_name}:\n    def __init__(self, *, client_wrapper: {wrapper_type}):\n        self._client_wrapper = client_wrapper"
    );
    for ep in endpoints {
        out.push_str("\n\n");
        out.push_str(&raw_method(ep, is_async, imports));
    }
    out
}

/// Render a named template with a context, mapping errors to [`Error::Render`].
fn render(
    env: &Environment<'static>,
    template: &str,
    file: &str,
    ctx: minijinja::value::Value,
) -> Result<String> {
    env.get_template(template)
        .and_then(|t| t.render(ctx))
        .map_err(|e| Error::Render {
            file: file.to_string(),
            message: e.to_string(),
        })
}

/// Remove the generated package tree (`src/<package>`) before a fresh write so
/// regeneration is idempotent: a schema or endpoint removed from the spec must
/// not leave an orphaned module behind. Scoped to `src/<package>`, which is
/// entirely crozier's own namespace — anything the user keeps elsewhere under
/// `--output` is untouched. A missing tree is not an error (first run).
pub fn clean_package_tree(root: &std::path::Path, package: &str) -> Result<()> {
    let tree = root.join("src").join(package);
    match std::fs::remove_dir_all(&tree) {
        Ok(()) => Ok(()),
        Err(source) if source.kind() == std::io::ErrorKind::NotFound => Ok(()),
        Err(source) => Err(Error::WriteOutput { path: tree, source }),
    }
}

/// Format every crozier-*generated* `.py` file in place with `ruff format`,
/// delegating line-wrapping to the tool Fern uses. Two classes of file are left
/// untouched:
///
/// - **Vendored `core/` runtime** — emitted verbatim from `assets/core/` (Fern's
///   own already-`ruff`-formatted source). Re-formatting would change it: since
///   these carry comments, `ruff format` then comment-strip does not commute with
///   Fern's strip-only fixture, so formatting them breaks the byte match.
/// - **Whitespace-only files** — the empty `py.typed` marker and the comment-only
///   endpoint `__init__.py` (emitted in its four-blank-line stripped form): there
///   is nothing to wrap and `ruff` would collapse them.
///
/// The lazy-loader `__init__.py` aggregators *are* formatted: they overflow (long
/// `_dynamic_imports`/`__all__`/import lines) and `ruff` wraps them like any other
/// file. Their leading blank lines collapse under `ruff`, but that is a
/// comment-strip artifact the e2e normalizes on both sides (see `normalize_init`),
/// so the byte match is preserved.
fn format_python_files(pkg: &str, files: &mut [GeneratedFile]) -> Result<()> {
    let core_root = PathBuf::from(format!("src/{pkg}/core"));
    for file in files.iter_mut() {
        let is_py = file.path.extension().and_then(|e| e.to_str()) == Some("py");
        let is_vendored = file.path.starts_with(&core_root);
        let has_code = file.contents.chars().any(|c| !c.is_whitespace());
        if is_py && !is_vendored && has_code {
            let name = file.path.to_string_lossy();
            file.contents =
                crate::pyfmt::format_source(&name, &file.contents, crate::pyfmt::LINE_LENGTH)?;
        }
    }
    Ok(())
}

/// Write each generated file under `root`, creating parent directories as needed.
pub fn write_files(root: &std::path::Path, files: &[GeneratedFile]) -> Result<()> {
    for file in files {
        let full = root.join(&file.path);
        if let Some(parent) = full.parent() {
            std::fs::create_dir_all(parent).map_err(|source| Error::WriteOutput {
                path: parent.to_path_buf(),
                source,
            })?;
        }
        std::fs::write(&full, &file.contents).map_err(|source| Error::WriteOutput {
            path: full.clone(),
            source,
        })?;
    }
    Ok(())
}

